-- "%.0f" rounds to the nearest integer; "%d" would truncate and read ~1 low.
local function UpdateFPS(self, fps)
  self.fpsText:SetText(("%.0f"):format(fps))
end

local function BuildFrame()
  local f = CreateFrame("Frame", "GraphitFrame", UIParent, "PortraitFrameFlatTemplate")
  -- First-ever size only; the corner floor clamps it up, and a saved geometry
  -- (RestoreGeometry) overrides it on every later open.
  f:SetSize(MIN_WIDTH, MIN_HEIGHT)
  f:SetPoint("CENTER")
  f:SetFrameStrata("HIGH")
  f:SetToplevel(true)
  f:EnableMouse(true)
  f:SetClampedToScreen(true)

  -- Persist the frame's size and position to the SavedVariable (account-wide).
  local function SaveGeometry()
    Graphit_config = Graphit_config or {}
    local point, _, relPoint, x, y = f:GetPoint(1)
