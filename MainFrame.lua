-- Apply the persisted expanded states (Graphit_config.expanded) to the built rows,
-- building any secondary's tertiary container that should start open; the caller relays
-- out afterward. A no-op until the SavedVariable has loaded, so the rows keep their
-- defaults (preset open, secondaries closed) until then.
local function RestoreExpandedStates(content)
  local saved = Graphit_config and Graphit_config.expanded
  if not saved then return end
  for _, row in ipairs(content.rows) do
    if row.expander and row.expandCvar and saved[row.expandCvar] ~= nil then
      local want = saved[row.expandCvar]
      if want and not row.isPreset then BuildTertiaries(row) end
      row.expanded = want
      SetExpanderAtlas(row.expander, want)
    end
  end
end

-- Re-sync every built tertiary control of a parent row to its (now cascaded) CVar.
-- Unbuilt (never-expanded) tertiaries need nothing: they Init from the live CVar.
local function RefreshTertiaries(row)
  local container = row.tertiaryContainer
  if not container or not container.tertiaryRows then return end
  for _, tertiaryRow in ipairs(container.tertiaryRows) do
    if tertiaryRow.Refresh then tertiaryRow.Refresh() end
  end
end

-- Re-read a secondary row from the live CVars after an external change -- specifically
-- the preset driving every secondary at once. A parent recomputes its level and refreshes
-- its own control plus any built tertiary controls; a plain setting re-reads its CVar.
-- The preset is reconciled separately (and last) so it inherits these fresh markers.
local function ResyncRow(row)
  if row.expandCvar then
    RefreshTertiaries(row)
    ReconcileParent(row)
  elseif row.Refresh then
    row.Refresh()
  end
end

-- A row is "warning" when it currently sits on an option flagged with a warning (Particle
-- Density = Disabled, which costs combat visual cues). row.warningValue / row.warningText
-- are set at build time from that option's data.
local function RowWarning(row)
  return row.warningValue ~= nil and row.cvar ~= nil
         and tonumber(LogicalValue(row.cvar)) == row.warningValue
end

-- Tint the name label of every row with a parked change or an active warning, and of its
-- parents: a deferred quality tertiary whose CVar sits in `pending`, its secondary, and --
-- if any quality tertiary is pending (or any quality secondary is on its warning option) -- the
-- preset. Built tertiary rows are tinted too; unbuilt ones pick it up when expanded
-- (ToggleExpand reruns this via RefreshApplyButton).
local function HighlightPending(content)
  if not content or not content.rows then return end
  -- The preset reflects only its own descendants -- a deferred quality tertiary, or a
  -- quality secondary on its warning option -- not a staged standalone setting (e.g. a
  -- Compatibility toggle), which also sits in `pending` but is unrelated to the preset.
  local qualityPending = false
  for _, tertiaries in pairs(Graphit.layer3) do
    for tertiaryCvar in pairs(tertiaries) do
      if pending[tertiaryCvar] then qualityPending = true break end
    end
    if qualityPending then break end
  end
  local qualityWarning, qualityWarningText = false, nil
  for _, row in ipairs(content.rows) do  -- a secondary (layer3 owner) sitting on its warning option
    if row.cvar and Graphit.layer3[row.cvar] and RowWarning(row) then
      qualityWarning, qualityWarningText = true, row.warningText
      break
    end
  end
  for _, row in ipairs(content.rows) do
    if row.isHeader or row.placeholder then
      -- no bound label to tint (a header has none; a placeholder stays greyed)
    elseif row.isPreset then
      row.warningText = qualityWarningText  -- so the preset's tooltip can explain the tint
      SetRowLabelPending(row, qualityPending, qualityWarning, false)
    elseif row.expandCvar and Graphit.layer3[row.expandCvar] then
      local secondaryPending = false
      for tertiaryCvar in pairs(Graphit.layer3[row.expandCvar]) do
        if pending[tertiaryCvar] then secondaryPending = true break end
      end
      SetRowLabelPending(row, secondaryPending, RowWarning(row), false)
      if row.tertiaryContainer and row.tertiaryContainer.tertiaryRows then
        for _, tertiaryRow in ipairs(row.tertiaryContainer.tertiaryRows) do
          SetRowLabelPending(tertiaryRow, pending[tertiaryRow.cvar] ~= nil, false, false)
        end
      end
    else
      -- a standalone setting may itself be staged (pending) or, for a
      -- clientRestart one, differ from the value the running client loaded (violet). A
      -- compound also stages its enable CVar (row.checkCvar), so honour that too.
      -- A row can also be staged "by proxy": its effective value follows another setting's
      -- uncommitted change (RenderScale driving Resample Quality / Sharpness). Cache the localised
      -- reason for the label tooltip, which shows it instead of the own-change freeze note.
      row.pendingProxyText = row.pendingProxy and FormatHint(row.pendingProxy())
      local staged = (row.cvar and pending[row.cvar] ~= nil)
                     or (row.checkCvar and pending[row.checkCvar] ~= nil)
                     or (row.pendingProxyText ~= nil) or false
      SetRowLabelPending(row, staged, RowWarning(row), row.cvar and IsRestartPending(row.cvar) or false)
    end
  end
end

-- Re-evaluate the rows whose control has an enableWhen precondition (the MSAA alpha
-- test, gated by Multisample AA) and re-tint the labels. Called right after a Graphit
-- edit so a dependent control reacts at once, not on the coalesced CVar watcher's delay.
local function RefreshEnableStates(content)
  if not content or not content.rows then return end
  for _, row in ipairs(content.rows) do
    if row.hasEnableWhen and row.Refresh then row.Refresh() end
  end
  HighlightPending(content)
end

-- Re-read every row from the live CVars and re-tint the pending highlight. Used
-- when the world state changed outside a single control: the saved-pending flush
-- and the live sync with the Settings panel.
local function RefreshAllRows(content)
  if not content or not content.rows then return end
  for _, r in ipairs(content.rows) do
    if not r.isPreset then ResyncRow(r) end
  end
  for _, r in ipairs(content.topRows or {}) do  -- the fixed top-section dropdowns
    if r.Refresh then r.Refresh() end
  end
  if content.presetRow then ReconcileParent(content.presetRow) end
  if RefreshApplyButton then RefreshApplyButton() end
end

-- ===== Confirm-apply countdown =====
--
-- A confirmApply setting (RenderScale) can crater FPS so hard the UI is barely usable. After
-- the Apply button commits one, ApplyPending snapshots the pre-apply values, persists them,
-- and pops a countdown: Keep drops the snapshot; Revert / Escape / the timeout restore it.
-- The snapshot lives in Graphit_config, so a force-close before deciding reverts on the next
-- clean load (ResolveSavedPending). A hard crash never flushes SavedVariables, so only a
-- clean exit (logout, /reload, Alt-F4) is recoverable.
local CONFIRM_APPLY_TIMEOUT = 15  -- seconds before an unconfirmed apply auto-reverts

local function RevertConfirmSnapshot()
  local snap = Graphit_config and Graphit_config.confirmSnapshot
  if not snap then return end
  Graphit_config.confirmSnapshot = nil
  CommitValues(snap)
  if frame and frame.content then RefreshAllRows(frame.content) end
  RefreshSettingsPanel()
end

StaticPopupDialogs["GRAPHIT_CONFIRM_APPLY"] = {
  text = "Keep these display settings?",
  subText = " ",  -- replaced each frame by OnUpdate with the live countdown
  button1 = "Keep",
  button2 = "Revert",
  -- Keep just drops the snapshot; Revert / Escape / the timeout all reach OnCancel and
  -- restore it (Graphit_config.confirmSnapshot is the single source of truth).
  OnAccept = function() if Graphit_config then Graphit_config.confirmSnapshot = nil end end,
  OnCancel = function() RevertConfirmSnapshot() end,
  OnUpdate = function(dialog) dialog.SubText:SetFormattedText("Reverting in %d seconds.", math.ceil(dialog.timeleft or 0)) end,
  timeout = CONFIRM_APPLY_TIMEOUT,
  whileDead = true,
  hideOnEscape = true,
  showAlert = true,
}

-- Drive a secondary to `level`. Normally one SetCVar lets the engine cascade to the
-- raw tertiaries. But when a tertiary is deferred, that cascade would change it too
-- (and freeze), so instead cascade by hand: set each non-deferred tertiary to its
-- preset and park the deferred one. The secondary CVar is deliberately left unset in
-- that case -- writing it would re-trigger the very cascade we are avoiding -- so
-- the secondary's level lives only in its row (which is why the preset reads row.level).
local function ApplySecondaryLevel(secondary, level)
  local matrix = Graphit.layer3[secondary]

  -- A single-tertiary secondary has no dumped tertiaries (it is a 1:1 alias), so just
  -- set it and let the engine cascade its one tertiary.
  if not matrix then
    SetCVar(secondary, tostring(level))
    return
  end

  local deferred = false
  for tertiaryCvar in pairs(matrix) do
    if IsDeferred(tertiaryCvar) then deferred = true break end
  end

  if not deferred then
    SetCVar(secondary, tostring(level))
    return
  end

  for tertiaryCvar, data in pairs(matrix) do
    if IsDeferred(tertiaryCvar) then
      SetPending(tertiaryCvar, data[level])
    else
      SetCVar(tertiaryCvar, tostring(data[level]))
    end
  end
end

-- The quality secondaries (layer2, minus the preset) are the settings Blizzard tags with a
-- "Recommended" marker -- its AddRecommended flags whichever option equals the CVar's
-- default. We mirror that, so this gates the recommended line in OptionDetailFunc.
local secondarySet
local function IsSecondary(cvar)
  if not secondarySet then
    secondarySet = {}
    for _, s in ipairs(Graphit.layer2) do
      if s.cvar and s.cvar ~= PRESET_CVAR then secondarySet[s.cvar] = true end
    end
  end
  return cvar ~= nil and secondarySet[cvar] or false
end

-- Mirror Blizzard's CreateOptionsInitTooltip: the block it appends to a control's tooltip
-- beyond the shared name + body. Returns a closure that adds, on hover: one
-- "Option: explanation" line per option that has a tooltip (the default greened on a
-- quality secondary) and the "Recommended: <default>" line for a secondary. Returns nil when there
-- is nothing to add, so a setting whose options carry no detail leaves the tooltip
-- unchanged. (The combat-cues warning is handled in AddLabelTooltip instead, since it also
-- tints the label and propagates to the preset.)
local function OptionDetailFunc(base)
  -- Resolve options now; their per-option tooltips are what we surface (e.g. Image-Based
  -- Antialiasing). Option-less controls (plain slider, CVar-less, MSAA's format) have none.
  local options = ResolveOptions(base.control)
  local secondary = IsSecondary(base.cvar)
  -- A setting may report a per-option availability (VRS, overruled by Multisample AA); the
  -- trailing hint / warning notes stand on their own, so a plain option-less slider (Resample
  -- Sharpness) still earns a detail function for them. All re-evaluated on hover.
  local optionDisabled = base.optionDisabled
  local hasDetail = secondary or optionDisabled ~= nil or base.optionsHint ~= nil or base.optionsWarning ~= nil
  if options then
    for _, o in ipairs(options) do
      if o.tooltip or o.unavailable then hasDetail = true end
    end
  end
  if not hasDetail then return nil end
  return function(tip)
    local default = base.cvar and GetCVarDefault and tonumber(GetCVarDefault(base.cvar))
    local recommended
    for _, o in ipairs(options or {}) do
      local isDefault = secondary and default ~= nil and o.value == default
      if isDefault then recommended = o end
      -- An unsupported option greys its name and explanation and shows the engine's reason in
      -- red directly below, as Blizzard's CreateOptionsInitTooltip does. The reason is either a
      -- live per-option check (VRS, re-checked on hover so it tracks Multisample AA) or the
      -- build-time o.unavailable tag from ValidatedOptions (the hardware-validated sliders).
      local disabled = (optionDisabled and optionDisabled(o.value)) or o.unavailable
      if o.tooltip or disabled then
        GameTooltip_AddBlankLineToTooltip(tip)
        -- Keep the ":" in the name's colour (white for an available option) rather than the
        -- line's grey base, so the separator matches the name, not the explanation.
        local label = (disabled and DISABLED_FONT_COLOR or HIGHLIGHT_FONT_COLOR):WrapTextInColorCode(Str(o.label) .. ":")
        if o.tooltip then
          local color = disabled and DISABLED_FONT_COLOR or (isDefault and GREEN_FONT_COLOR) or NORMAL_FONT_COLOR
          GameTooltip_AddDisabledLine(tip, label .. " " .. color:WrapTextInColorCode(Str(o.tooltip)))
        else
          GameTooltip_AddDisabledLine(tip, label)
        end
        if disabled then GameTooltip_AddErrorLine(tip, disabled) end
      end
    end
    if recommended then  -- the default option, greened, like Blizzard's recommended line
      GameTooltip_AddBlankLineToTooltip(tip)
      GameTooltip_AddHighlightLine(tip, string.format("%s: %s", VIDEO_OPTIONS_RECOMMENDED, GREEN_FONT_COLOR:WrapTextInColorCode(Str(recommended.label))))
    end
    AppendNotes(tip, base)  -- the always-on hint then the current-state warning, if any
  end
end

-- A compound checkbox+slider row, mirroring Blizzard's CreateSettingsCheckboxSliderInitializer
-- (the Max FPS caps and UI Scale): an enable checkbox followed by the value slider, which
-- greys out while the checkbox is off. The two share the row's label -- the checkbox drives
-- control.check, the slider the row's own CVar through `sliderBinding`. Both write at once,
-- unless the setting is deferApply (uiscale, whose live write rescales the UI and slides the
-- frame mid-drag): then both stage in `pending` for the Apply button, read pending-aware.
local function CreateCheckboxSlider(row, sliderBinding, base, parent)
  local control = base.control
  local checkCvar = control.check
  local deferred = IsDeferred(base.cvar)  -- stage the enable alongside the staged slider
  row.checkCvar = checkCvar               -- so HighlightPending tints on a staged enable

  local check = CreateFrame("CheckButton", nil, row, "UICheckButtonTemplate")
  check:SetSize(26, 26)
  check:SetPoint("LEFT", row.label, "RIGHT", 8, 0)

  -- The slider follows the checkbox, sharing the row's value binding. Carry over only the
  -- range/format fields; CreateSlider installs row.Refresh, which we wrap to add the grey.
  local slider = CreateSlider(row, sliderBinding, {
    min = control.min, max = control.max, step = control.step,
    minFunc = control.minFunc, maxFunc = control.maxFunc,
    labels = control.labels, format = control.format,
  }, { check, "RIGHT", 0 })
  local refreshSlider = row.Refresh

  row.hasEnableWhen = true  -- so RefreshEnableStates re-runs the grey state and re-tints
  row.Refresh = function()
    local on = tonumber(LogicalValue(checkCvar)) == 1  -- pending-aware while deferred
    check:SetChecked(on)
    refreshSlider()
    slider:SetEnabled(on)          -- greys the bar + steppers while the cap is off
    row.controlDisabled = not on   -- greys the label / readout to match
  end

  check:SetScript("OnClick", function(self)
    local on = self:GetChecked()
    PlaySound(on and SOUNDKIT.IG_MAINMENU_OPTION_CHECKBOX_ON or SOUNDKIT.IG_MAINMENU_OPTION_CHECKBOX_OFF)
    if deferred then
      SetPending(checkCvar, on and 1 or 0)
    else
      SetCVar(checkCvar, on and "1" or "0")
    end
    RefreshEnableStates(parent)
    if deferred and RefreshApplyButton then RefreshApplyButton() end
  end)

  row.Refresh()
end

-- One setting row: an expander (live for the preset and parents with tertiaries, a
-- greyed placeholder otherwise), name, then the control (slider or dropdown, live).
-- `base` is the secondary; its descriptor overrides are looked up by CVar.
-- The preset and any parent with tertiaries show a computed level; a plain
-- setting binds to its own CVar.
local function CreateRow(parent, base)
  base = MergeLogic(base)  -- overlay any Settings_Logic behaviour onto the Settings_Source mirror
  local row = CreateFrame("Frame", nil, parent)
  row:SetHeight(ROW_HEIGHT)

  AddHover(row)

  local isPreset = base.cvar == PRESET_CVAR
  row.cvar = base.cvar  -- so HighlightPending can test pending/restart by this row's CVar
  row.pendingProxy = base.pendingProxy  -- a staged dependency (RenderScale) can stage this by proxy
  -- A custom or other standalone setting sits at the preset's level, outside
  -- the collapse group.
  row.topLevel = base.topLevel or base.custom

  -- The preset is a parent (it reconciles a level like the secondaries); its expander
  -- shows or hides the secondary rows below it (its "tertiaries"), expanded by default.
  -- Expander column: the preset and any secondary with tertiaries get a working "+"; a childless
  -- secondary keeps a greyed placeholder so its name stays aligned with its siblings in the Quality
  -- group. A standalone / custom setting is not in that group and never has tertiaries, so
  -- it drops the column entirely -- its name starts flush left, the freed width folded into the
  -- name column (the control column stays put).
  local labelLeft, labelWidth = EXPANDER_WIDTH, LABEL_WIDTH
  if isPreset then
    row.expandCvar     = base.cvar
    row.isPreset = true
    row.expanded = true
    CreateExpander(row)

  -- "+" expander for settings that have dumped tertiaries.
  elseif Graphit.layer3 and Graphit.layer3[base.cvar] then
    row.expandCvar = base.cvar
    local d = Graphit.descriptor
    row.tertiaryOrder = d and d.tertiaryOrder and d.tertiaryOrder[base.cvar]
    row.expanded = false
    CreateExpander(row)

  elseif row.topLevel then
    labelLeft, labelWidth = LABEL_LEFT_PAD, LABEL_WIDTH + EXPANDER_WIDTH - LABEL_LEFT_PAD

  -- A secondary with no tertiaries: a greyed-out expander, for column symmetry only.
  else
    CreatePlaceholderExpander(row)
  end

  -- A descriptor override may rename the row (overrides[cvar].name); else the mirror's name.
  -- A literal or a global-string key, resolved through Str like any other name.
  local override = OverrideFor(base.cvar)
  local displayName = (override and override.name) or base.name

  row.label = row:CreateFontString(nil, "ARTWORK", "GameFontNormal")
  row.label:SetPoint("LEFT", labelLeft, 0)
  row.label:SetWidth(labelWidth)
  row.label:SetJustifyH("LEFT")
  row.label:SetWordWrap(true)  -- long names wrap (left-aligned) instead of truncating
  row.labelColor = NORMAL_FONT_COLOR  -- GameFontNormal's own color; turned to warning when pending
  row.label:SetText(Str(displayName))

  -- A placeholder row -- a setting we have not wired up yet (runtime-driven, or a
  -- control shape we do not render yet). Greyed name, an em-dash where the control
  -- will sit, no binding; the hover still names it.
  if base.placeholder then
    row.placeholder = true
    row.label:SetTextColor(DISABLED_FONT_COLOR:GetRGB())
    local note = row:CreateFontString(nil, "ARTWORK", "GameFontDisableSmall")
    note:SetPoint("RIGHT", row, "RIGHT", -8, 0)
    note:SetText("—")
    AddLabelTooltip(row, Str(displayName), "Not yet available in Graphit.")
    return row
  end

  -- Three binding shapes:
  --   * Preset: shows a computed level; driving it applies every secondary
  --     (ApplySecondaryLevel; graphicsQuality has no SetCVar cascade of its own) and,
  --     once the per-secondary cascades settle, resyncs all rows -- which reconciles
  --     the preset itself too.
  --   * Secondary parent: shows a computed level; driving it applies the secondary
  --     (ApplySecondaryLevel, which cascades to the tertiaries or, if one is deferred,
  --     parks it), then refreshes tertiaries, re-reconciles, and re-reconciles the
  --     preset a tier up.
  --   * Plain setting: binds to its own CVar; an edit still re-reconciles the
  --     preset, whose level tracks the secondary levels.
  local binding
  if row.isPreset then
    binding = {
      get = function() return row.level end,
      set = function(v)
        local level = tonumber(v)
        row.level = level
        row.exceeds = false
        row.below = false
        row.approx = false
        for secondaryCvar, data in pairs(Graphit.layer3[row.expandCvar]) do
          ApplySecondaryLevel(secondaryCvar, tonumber(data[level]))
        end
        SetCVar(row.expandCvar, tostring(level))  -- keep the preset's own CVar coherent
        if RefreshApplyButton then RefreshApplyButton() end
        C_Timer.After(CASCADE_SETTLE, function()
          for _, r in ipairs(parent.rows) do
            if not r.isPreset then ResyncRow(r) end
          end
          ReconcileParent(row)  -- preset last, so it inherits the secondaries' fresh markers
        end)
      end,
    }
  elseif row.expandCvar then
    binding = {
      get = function() return row.level end,
      set = function(v)
        row.level = tonumber(v)
        row.exceeds = false
        row.below = false
        row.approx = false
        ApplySecondaryLevel(row.expandCvar, tonumber(v))
        if RefreshApplyButton then RefreshApplyButton() end
        C_Timer.After(CASCADE_SETTLE, function()
          RefreshTertiaries(row)
          ReconcileParent(row)
          if parent.presetRow then ReconcileParent(parent.presetRow) end
        end)
      end,
    }
  elseif base.read then
    -- A CVar-less custom setting (Resolution): read / written through closures over
    -- C_VideoOptions, not a single CVar. Applies at once -- "windowUpdate" refreshes the
    -- window, and a full re-read updates dependent rows (the Render Scale label, whose
    -- text is the resolution at its scale).
    binding = {
      get = base.read,
      set = function(v)
        base.write(v)
        if base.commit == "windowUpdate" then UpdateWindow() end
        RefreshAllRows(parent)
      end,
    }
  else
    -- A plain setting binds to its own CVar. How the write lands depends on its commit
    -- flag: "live" applies at once; "windowUpdate" applies and refreshes the window
    -- (light, no freeze); "gxRestart" is staged for the Apply button (ApplyPending runs
    -- the device restart once on commit), since that restart may be heavy and we have
    -- not confirmed it is freeze-free; "clientRestart" is written now but only takes
    -- effect after a full game restart, so the row is tinted violet meanwhile. A
    -- deferApply override (Settings_Custom) stages a setting for the Apply button too,
    -- regardless of its commit flag (uiscale, to avoid rescaling the UI mid-drag).
    binding = {
      -- A custom get transforms the pending-aware raw CVar value (e.g. MSAA splits off
      -- the msaa part, Graphics API case-folds and matches the API list).
      get = base.get and function() return base.get(LogicalValue(base.cvar)) end
            or function() return LogicalValue(base.cvar) end,
      set = function(v)
        -- A custom set transforms the control value into the CVar string to write (e.g.
        -- Triple Buffering's 0/1 checkbox maps to gxMaxFrameLatency 2/3).
        local cvarValue = base.set and base.set(v) or tostring(v)
        local commit = base.commit
        if commit == "gxRestart" or IsDeferred(base.cvar) then
          -- Staged for the Apply button: a gxRestart device reset, or a deferApply CVar
          -- whose live write is disruptive mid-edit (uiscale rescales the UI, sliding the
          -- frame out from under the slider).
          SetPending(base.cvar, cvarValue)
          if RefreshApplyButton then RefreshApplyButton() end
          -- A staged value can still be a precondition for another control (RenderScale greys
          -- Resample Quality / Sharpness), so re-evaluate the dependent enableWhen rows now.
          RefreshEnableStates(parent)
        else
          SetCVar(base.cvar, cvarValue)
          if commit == "windowUpdate" then UpdateWindow() end
          if parent.presetRow then ReconcileParent(parent.presetRow) end
          -- Apply at once: refresh any dependent controls (enableWhen) and re-tint the
          -- labels now (a clientRestart row goes violet here), rather than waiting for
          -- the coalesced CVar watcher.
          RefreshEnableStates(parent)
        end
      end,
    }
  end

  -- A custom setting carries its own control verbatim. Otherwise default to a slider
  -- (SliderFromControl keeps a dropdown's labels on the readout); a descriptor
  -- override forces another kind, and a dropdown override reuses the Blizzard mirror's
  -- options unless it lists its own. (override resolved above, with the name.)
  local control
  if base.custom then
    control = base.control
  elseif override and override.control and override.control.kind == "dropdown" then
    -- An explicit dropdown override stays a real dropdown (options from the override,
    -- else the Blizzard mirror's), rather than being turned into a slider.
    local oc = override.control
    control = { kind = "dropdown", options = oc.options or base.control.options, betterIsLower = oc.betterIsLower }
  else
    -- The effective control: the base with any override merged over it -- so an override
    -- states only what changes, e.g. control = { step = 1 }. Then resolve the runtime
    -- list and the slider-by-default: a categorical optionsFunc dropdown (Monitor) stays
    -- a dropdown; anything else (a static dropdown, or an optionsFunc quality slider like
    -- MSAA) becomes an enumerated slider via SliderFromControl.
    local merged = (override and override.control) and MergedControl(base.control, override.control) or base.control
    if merged.optionsFunc then
      -- Resolve the runtime list, dropping options the hardware does not support (a slider /
      -- dropdown cannot hold an unsupported stop). The dropped options still surface in the
      -- label tooltip -- greyed, with the engine's reason in red -- via OptionDetailFunc, which
      -- reads the same o.unavailable tag. A categorical list stays a dropdown; a quality scale
      -- (kind "slider", e.g. Ray Traced Shadows) becomes an enumerated slider.
      local avail = {}
      for _, o in ipairs(merged.optionsFunc()) do
        if not o.unavailable then avail[#avail + 1] = o end
      end
      if merged.kind == "dropdown" then
        control = { kind = "dropdown", options = avail, betterIsLower = merged.betterIsLower }
      else
        control = SliderFromControl({ kind = "slider", options = avail, betterIsLower = merged.betterIsLower })
      end
    else
      control = SliderFromControl(merged)
    end
  end
  if control.kind == "slider" then
    if control.values and #control.values <= 1 then
      CreateStaticReadout(row, control.values[1] and control.labels[control.values[1]])
    else
      CreateSlider(row, binding, control, nil, base.enableWhen)
    end
  elseif control.kind == "dropdown" then
    if #control.options <= 1 then
      CreateStaticReadout(row, control.options[1] and Str(control.options[1].label))
    else
      local opts = {}
      for _, o in ipairs(control.options) do
        opts[#opts + 1] = { value = o.value, text = Str(o.label) }
      end
      CreateDropdown(row, binding, opts, control.betterIsLower,
                     base.enableWhen, base.disabledTooltip and Str(base.disabledTooltip))
    end
  elseif control.kind == "checkbox" then
    CreateCheckbox(row, binding, base.enableWhen)
  elseif control.kind == "checkboxSlider" then
    CreateCheckboxSlider(row, binding, base, parent)
  end

  -- Tooltip: a descriptor override wins (literal text or a key, via Str); otherwise
  -- the Blizzard mirror's key, resolved strictly -- an undefined one (e.g. Blizzard
  -- references OPTION_TOOLTIP_GRAPHICS_QUALITY but never defines it) shows nothing,
  -- not its raw name.
  local tip
  if base.custom then
    tip = base.tooltip and Str(base.tooltip)
  elseif override and override.tooltip then
    tip = Str(override.tooltip)
  elseif base.tooltip then
    tip = _G[base.tooltip]
  end
  -- A warning option (Particle Density = Disabled) tints the row and its preset; record its
  -- trigger value and text for HighlightPending / RowWarning and the tooltip note.
  local detailOptions = ResolveOptions(base.control)
  if detailOptions then
    for _, o in ipairs(detailOptions) do
      if o.warning then row.warningValue = o.value; row.warningText = Str(o.warning); break end
    end
  end
  local labelHit = AddLabelTooltip(row, Str(displayName), tip, OptionDetailFunc(base))
  -- A click on the name toggles expand/collapse too, matching the chevron.
  if row.expander then
    labelHit:SetScript("OnClick", function() ToggleExpand(row) end)
  end

  -- Seed the parent's displayed level and markers from the current tertiary CVars.
  if row.expandCvar then ReconcileParent(row) end
  return row
end

-- A section-header row: the label sits low; a thin divider above it separates it from the
-- section before (lineAbove -- the tab's first header passes false, so it gets none). Flush and
-- always shown (no control, not interactive), laid out by Relayout like the preset.
local function CreateHeaderRow(parent, headerKey, info, lineAbove)
  local row = CreateFrame("Frame", nil, parent)
  row:SetHeight(HEADER_ROW_HEIGHT)
  row.isHeader = true
  row.lineAbove = lineAbove  -- false for the tab's first header: no divider and no SECTION_GAP

  local fs = row:CreateFontString(nil, "ARTWORK", "GameFontNormalLarge")
  fs:SetPoint("BOTTOMLEFT", 2, 6)
  fs:SetTextColor(WHITE_FONT_COLOR:GetRGB())  -- white, like the Settings panel's section headers
  fs:SetText(Str(headerKey))

  -- An optional right-aligned info "i" (the category's `info` in Settings_Custom): hover-only,
  -- shows that text as a tooltip -- no click, no sound. Absent when the category defines no info.
  if info then
    local btn = CreateFrame("Button", nil, row)
    btn:SetSize(30, 30)
    -- Directly right of the title so it tracks the title's length at runtime; the -8 drops it to
    -- sit level with the title text (which is anchored 6px up from the row's bottom).
    btn:SetPoint("BOTTOMLEFT", fs, "BOTTOMRIGHT", 0, -8)
    btn:SetNormalTexture("Interface\\common\\help-i")
    -- Rather not use hover glow. It gives the impression the button is clickable which it is not.
    -- btn:SetHighlightAtlas("ChallengeMode-KeystoneSlotFrameGlow")
    btn:SetScript("OnEnter", function(self)
      GameTooltip:SetOwner(self, "ANCHOR_RIGHT")
      GameTooltip_SetTitle(GameTooltip, Str(headerKey))
      GameTooltip_AddNormalLine(GameTooltip, Str(info), true)
      GameTooltip:Show()
    end)
    btn:SetScript("OnLeave", GameTooltip_Hide)
  end

  if lineAbove then
    local line = row:CreateTexture(nil, "ARTWORK")
    line:SetColorTexture(1, 1, 1, 0.15)
    line:SetHeight(1)
    line:SetPoint("TOPLEFT", row, "TOPLEFT", 0, -1)
    line:SetPoint("TOPRIGHT", row, "TOPRIGHT", -4, -1)
  end
  return row
end

-- Build every row (stored on content.rows) by walking the layer1 layout: section
-- headers, the Graphics Quality group (the preset plus its secondary rows and any
-- custom rows), and the standalone settings of the other sections. Within the
-- Quality group the descriptor's `secondaryOrder` still leads, the rest follow Blizzard order.
local function BuildRows(content)
  content.rows = {}
  content.rowByCvar = {}  -- cvar -> row, for the preset's per-secondary level lookup

  local d = Graphit.descriptor

  -- Index every setting by CVar: Blizzard's layer2 Quality mirror, the standalone
  -- standalone settings (the other sections), and any custom settings from
  -- Settings_Custom. The latter two are flagged topLevel so they sit at the preset's
  -- level; a custom entry is also flagged so CreateRow uses its control verbatim.
  local base = {}
  for _, s in ipairs(Graphit.layer2) do base[s.cvar] = s end
  for _, s in ipairs(Graphit.layer1) do
    if s.cvar then s.topLevel = true; base[s.cvar] = s end
  end
  if d and d.custom then
    for _, s in ipairs(d.custom) do s.custom = true; s.topLevel = true; base[s.cvar] = s end
  end

  -- Rows are tagged with the tab currently being built; Relayout lays out only the active one.
  local currentTab = 1

  -- Create and register one setting's row (skipping a gated one that is absent).
  local function AddSettingRow(s)
    if not s or (s.gate and not s.gate()) then return end
    local row = CreateRow(content, s)
    row.tab = currentTab
    content.rows[#content.rows + 1] = row
    if s.cvar then content.rowByCvar[s.cvar] = row end  -- a CVar-less row (Resolution) has no key
    return row
  end

  local function AddHeaderRow(headerKey, info, lineAbove)
    local row = CreateHeaderRow(content, headerKey, info, lineAbove)
    row.tab = currentTab
    content.rows[#content.rows + 1] = row
  end

  -- The Graphics Quality group: the preset leads, then the secondaries the descriptor's `secondaryOrder`
  -- hoists, then the rest in Blizzard order. (Custom rows are placed by tabGroups like any setting.)
  local function AddQualityGroup()
    local seen, order = {}, {}
    if base[PRESET_CVAR] then order[#order + 1] = PRESET_CVAR; seen[PRESET_CVAR] = true end
    if d and d.secondaryOrder then
      for _, cvar in ipairs(d.secondaryOrder) do
        if base[cvar] and not seen[cvar] then order[#order + 1] = cvar; seen[cvar] = true end
      end
    end
    for _, s in ipairs(Graphit.layer2) do
      if not seen[s.cvar] then order[#order + 1] = s.cvar; seen[s.cvar] = true end
    end
    for _, cvar in ipairs(order) do
      local row = AddSettingRow(base[cvar])
      if row and cvar == PRESET_CVAR then content.presetRow = row end
    end
  end

  -- Each tab is a flat, ordered list rendered top to bottom. An item is one of: a CVar name
  -- (string); a header { header = "KEY", info? } -- shown lazily, only once a setting or the
  -- quality group actually appears under it; or { quality = true }, the Graphics Quality group
  -- (preset + secondaries). The fixed top-band settings (BuildTopSection) are not listed here.
  if d and d.tabGroups then
    local indices = {}
    for tabIndex in pairs(d.tabGroups) do indices[#indices + 1] = tabIndex end
    table.sort(indices)
    for _, tabIndex in ipairs(indices) do
      currentTab = tabIndex
      local tabHasHeader = false  -- the tab's first header gets no divider above it
      local pendingHeader  -- a header waits here until a setting / the quality group appears under it
      local function FlushHeader()
        if pendingHeader then
          AddHeaderRow(pendingHeader.header, pendingHeader.info, tabHasHeader)
          tabHasHeader, pendingHeader = true, nil
        end
      end
      for _, item in ipairs(d.tabGroups[tabIndex]) do
        if type(item) == "string" then
          local s = base[item]
          if s and not (s.gate and not s.gate()) then FlushHeader(); AddSettingRow(s) end
        elseif item.quality then
          FlushHeader(); AddQualityGroup()
        elseif item.header then
          pendingHeader = item  -- a new header supersedes an unflushed (empty) one
        end
      end
    end
  end

  -- Safety net: a standalone (or custom) setting that tabGroups did not place -- e.g. a new one
  -- from a patch we have not categorised yet -- still shows up, appended to the catch-all tab so
  -- nothing silently vanishes; likewise the Quality group if no tab claimed it.
  currentTab = CATCHALL_TAB
  for _, item in ipairs(Graphit.layer1) do
    if item.cvar and not content.rowByCvar[item.cvar] and not IsTopSectionSetting(item) then
      AddSettingRow(item)
    end
  end
  if d and d.custom then
    for _, s in ipairs(d.custom) do
      if not content.rowByCvar[s.cvar] then AddSettingRow(s) end
    end
  end
  if not content.presetRow then AddQualityGroup() end

  -- Each row seeded only its own markers; reconcile the preset once more now that
  -- all secondary rows exist, so it inherits any that are already deviating.
  if content.presetRow then ReconcileParent(content.presetRow) end
  RestoreExpandedStates(content)
  Relayout(content)
end

