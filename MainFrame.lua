  local function SaveGeometry()
    Graphit_config = Graphit_config or {}
    local point, _, relPoint, x, y = f:GetPoint(1)
    Graphit_config.geometry = {
      point = point, relPoint = relPoint, x = x, y = y,
      width = f:GetWidth(), height = f:GetHeight(),
    }
  end

  -- ===== Frame chrome: a plain flat panel, assembled from the same three pieces the Blizzard
  -- Settings frame is -- a flat background, a portrait-less metal nine-slice, a centred title,
  -- and a close button. =====
  -- Flat dark background, inset like the Settings panel's own Bg.
