-- Every row registers a Settings-style hover highlight here; the frame's
-- OnUpdate shows the one under the cursor (this works while hovering the
-- child controls too, which a plain OnEnter/OnLeave on the row would not).
local hoverRows = {}

local function AddHover(row)
  row.highlight = row:CreateTexture(nil, "ARTWORK")
  row.highlight:SetColorTexture(1, 1, 1, 0.1)  -- same as the Settings HoverBackground
  row.highlight:SetAllPoints(row)
  row.highlight:Hide()
  hoverRows[#hoverRows + 1] = row
end

-- Violet: a setting changed that the running game applies only after a client restart.
local RESTART_FONT_COLOR = CreateColor(0.78, 0.51, 0.97)

-- The Apply button's icon; the pending tooltip embeds it inline so the text points at the
-- exact button to click.
local REVERT_ATLAS = "transmog-icon-revert"

-- Make a row's name label the hover region for a tooltip, as in the Settings panel.
-- The trigger is a button over the label (a FontString takes no mouse). Content is
-- built on hover so it tracks the live pending state: the title (always), the
-- setting's own tooltip body (if any), and -- while the row carries the pending
-- highlight -- the explanation appended at the end. A row with neither a body nor
-- the highlight shows nothing. Returns the button so the caller can also bind a
-- click (an expandable row toggles on a label click, like its chevron).
local function AddLabelTooltip(row, title, body, extra, appendFunc)
  local hasBody = body and body ~= ""
  local hit = CreateFrame("Button", nil, row)
  -- Match the label's width but span the full row height, so the hover/click area
  -- lines up with the row's own hover highlight. The label is centred in the row,
  -- so reach half the row's height above and below its left/right edges.
  local half = row:GetHeight() / 2
  hit:SetPoint("TOPLEFT", row.label, "LEFT", 0, half)
  hit:SetPoint("BOTTOMRIGHT", row.label, "RIGHT", 0, -half)
  hit:SetScript("OnEnter", function(self)
    if not hasBody and not extra and not appendFunc and not row.pendingHighlight and not row.warningHighlight and not row.restartHighlight then return end
    GameTooltip:SetOwner(self, "ANCHOR_RIGHT")
    GameTooltip_SetTitle(GameTooltip, title)
    if hasBody then
      GameTooltip_AddNormalLine(GameTooltip, body, true)
    end
    -- Blizzard's control tooltip is our name tooltip plus a per-option detail block; mimic
    -- it here (the per-option explanations and the recommended default).
    if appendFunc then appendFunc(GameTooltip) end
    if extra then  -- greyed supplementary block (e.g. unavailable options and why)
      if hasBody then GameTooltip_AddBlankLineToTooltip(GameTooltip) end
      GameTooltip:AddLine(extra, DISABLED_FONT_COLOR.r, DISABLED_FONT_COLOR.g, DISABLED_FONT_COLOR.b, true)
    end
    -- Pending and warning can both apply (the master may inherit both); show each in its
    -- own colour, warning first to match the dominant label tint. A blank line separates
    -- each from what precedes (always at least the title).
    if row.warningHighlight and row.warningText then
      GameTooltip_AddBlankLineToTooltip(GameTooltip)
      GameTooltip:AddLine(row.warningText, WARNING_FONT_COLOR.r, WARNING_FONT_COLOR.g, WARNING_FONT_COLOR.b, true)
    end
    if row.pendingHighlight then
      GameTooltip_AddBlankLineToTooltip(GameTooltip)
      GameTooltip:AddLine("A change is pending that might briefly freeze the game or change the UI layout. Click the flashing " .. CreateAtlasMarkup(REVERT_ATLAS, 20, 20) .. " button above to apply it.", ORANGE_FONT_COLOR.r, ORANGE_FONT_COLOR.g, ORANGE_FONT_COLOR.b, true)
    end
    if row.restartHighlight then
      GameTooltip_AddBlankLineToTooltip(GameTooltip)
      GameTooltip:AddLine(VIDEO_OPTIONS_NEED_CLIENTRESTART, RESTART_FONT_COLOR.r, RESTART_FONT_COLOR.g, RESTART_FONT_COLOR.b, true)
    end
    GameTooltip:Show()
  end)
  hit:SetScript("OnLeave", GameTooltip_Hide)
  return hit
end

-- ===== Controls =====

-- The marker affixes a parent row wears for deviating from its level: a leading
-- "~" when a "free" child is off-preset (approx) and a trailing "-" (a child fell
-- below the lowest preset) or "+" (children ran past the level), "-" winning.
-- Shared by the slider and dropdown so both render markers identically.
local function MarkerAffixes(row)
  local prefix = row.approx and "~" or ""
  local suffix = (row.below and "-") or (row.exceeds and "+") or ""
  return prefix, suffix
end

-- A modern Settings-style slider bound to a `binding` {get, set}: get() returns
-- the value to show, set(v) (a number) is called on user input. Dragging or
-- stepping is live, so the cascade is the preview. displayOffset gives Blizzard's
-- 1-10; the readout also carries the parent's deviation markers (MarkerAffixes).
--
-- spec.labels (value -> text) names the readout instead of the number, so a
-- setting whose Blizzard control was a labelled dropdown reads as a slider that
-- still shows Low / Medium / High etc.
--
-- spec.betterIsLower flips the bar so the high-quality (lower) end is on the
-- right: the slider value mirrors the raw CVar around the range (span - raw),
-- while the readout and the binding stay the true CVar value. Dragging or
-- stepping right then lowers the raw value, counting the readout down.
local function CreateSlider(row, binding, spec, leftAnchor)
  local widget = CreateFrame("Frame", nil, row, "MinimalSliderWithSteppersTemplate")
  widget:SetHeight(20)
  -- The bar's left sits after the label, or after a preceding control (the checkbox of a
  -- compound checkbox+slider row) when the caller passes a { frame, point, x } anchor.
  local lf, lp, lx = row.label, "RIGHT", 8
  if leftAnchor then lf, lp, lx = leftAnchor[1], leftAnchor[2], leftAnchor[3] end
  widget:SetPoint("LEFT", lf, lp, lx, 0)
  widget:SetPoint("RIGHT", row, "RIGHT", -SLIDER_VALUE_GAP, 0)  -- room for RightText

  -- A runtime-ranged slider (e.g. Camera FOV) resolves its bounds at build time.
  local offset = spec.displayOffset or 0
  local labels = spec.labels
  local values = spec.values  -- enumerated: the bar indexes into this ordered list of
                              -- valid raw values, so a non-uniform set (MSAA 0/2/4/8)
                              -- still snaps cleanly; otherwise it is a numeric range.
  local minVal, maxVal, steps, decimals, toSlider, toRaw, snap

  if values then
    local n = #values
    local indexOf = {}
    for i, v in ipairs(values) do indexOf[v] = i - 1 end
    minVal, maxVal, steps, decimals = 0, n - 1, n - 1, 0
    toSlider = function(raw)
      local idx = indexOf[tonumber(raw)] or 0
      return spec.betterIsLower and (n - 1 - idx) or idx
    end
    toRaw = function(pos)
      local idx = math.max(0, math.min(n - 1, math.floor(pos + 0.5)))
      if spec.betterIsLower then idx = n - 1 - idx end
      return values[idx + 1]
    end
    snap = function(raw) return raw end  -- toRaw already yields a value from the list
  else
    -- A static min/max wins over the runtime minFunc/maxFunc, so an override can pin
    -- the range (control = { min = .., max = .. }) on a setting that defaults to one.
    minVal = spec.min or (spec.minFunc and spec.minFunc())
    maxVal = spec.max or (spec.maxFunc and spec.maxFunc())
    local step = spec.step or 1
    steps = (maxVal - minVal) / step
    decimals = 0
    do local frac = tostring(step):match("%.(%d+)"); if frac then decimals = #frac end end
    local span = spec.betterIsLower and (minVal + maxVal) or nil
    toSlider = function(raw)         return span and (span - raw) or raw end
    toRaw    = function(sliderValue) return span and (span - sliderValue) or sliderValue end
    -- Snap a raw value to the step grid: an integer step rounds to a whole number (so the
    -- CVar is written "3", not "3.0"); a fractional step (Gamma's 0.1) keeps as many
    -- decimals as the step has -- which is also how the readout formats the number.
    snap = function(raw)
      local v = minVal + math.floor((raw - minVal) / step + 0.5) * step
      if decimals == 0 then return math.floor(v + 0.5) end
      return tonumber(("%." .. decimals .. "f"):format(v))
    end
  end
  local defaultRaw = values and values[1] or minVal

  local function FormatRaw(raw)
    if labels and labels[raw] then return labels[raw] end
    if spec.format then return spec.format(raw + offset) end  -- e.g. "60 FPS", "90%"
    if decimals > 0 then return ("%." .. decimals .. "f"):format(raw + offset) end
    return tostring(raw + offset)
  end

  local formatters = {
    [MinimalSliderWithSteppersMixin.Label.Right] =
      function(value)
        local prefix, suffix = MarkerAffixes(row)
        return prefix .. FormatRaw(snap(toRaw(value))) .. suffix
      end,
  }

  -- Init wires its OnValueChanged after the initial SetValue, so this does
  -- not echo back to the binding.
  widget:Init(toSlider(tonumber(binding.get()) or defaultRaw), minVal, maxVal, steps, formatters)

  -- Let a long readout (e.g. "Ultra High") wrap onto a second line within the
  -- reserved gap instead of overflowing over the bar. The template anchors RightText
  -- with no width, so it grows sideways; re-anchor it to the row's right with the gap
  -- as its width budget and turn on word wrap (which breaks at spaces). A single
  -- over-long word still truncates with "..."; the hover below shows its full text.
  local rt = widget.RightText
  rt:ClearAllPoints()
  rt:SetPoint("RIGHT", row, "RIGHT", -4, 0)
  rt:SetWidth(SLIDER_VALUE_GAP - 6)
  rt:SetJustifyH("CENTER")
  rt:SetWordWrap(true)

  -- Colour the readout to match the row's name (grey for a Layer-3 child). The
  -- template tints RightText via SetVertexColor, so clear that and set the text
  -- colour directly; row.readout lets the pending highlight re-colour it in step.
  rt:SetVertexColor(1, 1, 1)
  rt:SetTextColor(row.labelColor:GetRGB())
  row.readout = rt

  -- A truncated readout shows its full text in a tooltip on hover -- the labels are
  -- runtime global strings, so a per-locale hand-break is not practical.
  local rtHit = CreateFrame("Frame", nil, row)
  rtHit:SetAllPoints(rt)
  rtHit:EnableMouse(true)
  rtHit:SetScript("OnEnter", function(self)
    if not rt:IsTruncated() then return end
    GameTooltip:SetOwner(self, "ANCHOR_RIGHT")
    GameTooltip_SetTitle(GameTooltip, rt:GetText())
    GameTooltip:Show()
  end)
  rtHit:SetScript("OnLeave", GameTooltip_Hide)

  local refreshing = false
  widget:RegisterCallback(MinimalSliderWithSteppersMixin.Event.OnValueChanged,
    function(_, value)
      if refreshing then return end  -- a display re-sync, not a user edit
      binding.set(snap(toRaw(value)))
    end, row)

  -- Re-render from the binding (e.g. after a cascade) without writing it back;
  -- the formatter picks up the marker flags (row.exceeds / row.below / row.approx).
  -- SetValue is a no-op when the value is unchanged (so it would not refresh a
  -- marker), hence the explicit FormatValue.
  row.Refresh = function()
    refreshing = true
    widget:SetValue(toSlider(tonumber(binding.get()) or defaultRaw))
    widget:FormatValue(widget.Slider:GetValue())
    refreshing = false
  end
  return widget
end

-- The Settings panel's dropdown control bound to `binding` {get, set}: a
-- WowStyle2 dropdown flanked by < > stepper buttons. `options` is a list of
-- { value, text }; the steppers move to the prev/next option (disabled at the
-- ends) and picking calls set(value). The button shows whichever option matches
-- get(), with the parent's deviation markers affixed (MarkerAffixes).
--
-- betterIsLower reverses the option order so the menu runs worst-to-best and the
-- Forward (>) stepper moves toward better, matching the reversed slider.
local function CreateDropdown(row, binding, options, betterIsLower, enableWhen, disabledTooltip)
  local dd  = CreateFrame("DropdownButton", nil, row, "WowStyle2DropdownTemplate")
  local dec = CreateFrame("Button", nil, row, "WowStyle2IconButtonTemplate")
  local inc = CreateFrame("Button", nil, row, "WowStyle2IconButtonTemplate")

  -- Layout: [<] [ dropdown fills ] [>]
  dec:SetPoint("LEFT", row.label, "RIGHT", 4, 0)
  inc:SetPoint("RIGHT", row, "RIGHT", -6, 0)
  dd:SetPoint("LEFT", dec, "RIGHT", 2, 0)
  dd:SetPoint("RIGHT", inc, "LEFT", -2, 0)

  dec.normalAtlas, dec.disabledAtlas = "common-dropdown-icon-back", "common-dropdown-icon-back-disabled"
  inc.normalAtlas, inc.disabledAtlas = "common-dropdown-icon-next", "common-dropdown-icon-next-disabled"
  dec:OnButtonStateChanged()
  inc:OnButtonStateChanged()

  -- For betterIsLower, present the options worst-to-best (reverse the value order)
  -- so the steppers run the same way as the reversed slider.
  if betterIsLower then
    local reversed = {}
    for i = #options, 1, -1 do reversed[#reversed + 1] = options[i] end
    options = reversed
  end

  -- Affix the parent's deviation markers to the shown value (nil falls back to the
  -- default selection text).
  dd:SetSelectionText(function(selections)
    local sel = selections and selections[1]
    local prefix, suffix = MarkerAffixes(row)
    local text
    if not sel then
      -- No option matches the current value (e.g. a non-standard / maximised resolution):
      -- show the actual current value rather than leaving it blank.
      local current = binding.get()
      text = (current and current ~= "") and (prefix .. tostring(current) .. suffix) or nil
    else
      text = prefix .. MenuUtil.GetElementText(sel) .. suffix
    end
    -- Grey the text while disabled -- a flat disabled colour like the steppers and the
    -- label, not a faded alpha.
    if text and enableWhen and not enableWhen() then
      text = DISABLED_FONT_COLOR:WrapTextInColorCode(text)
    end
    return text
  end)

  dd:SetupMenu(function(_, rootDescription)
    for _, opt in ipairs(options) do
      -- CreateHighlightRadio (not CreateRadio) is what the Settings panel uses: the
      -- selected entry is shown in gold text, with no radio dot.
      rootDescription:CreateHighlightRadio(opt.text,
        function() return tostring(binding.get()) == tostring(opt.value) end,
        function() binding.set(opt.value) end)
    end
  end)

  -- Grey out a stepper at the ends of the option list; both go dead while the control is
  -- disabled. dd:Increment/Decrement do NOT fire OnUpdate, so refresh explicitly after a
  -- stepper click as well.
  local function Enabled() return (not enableWhen) or enableWhen() end
  local function UpdateSteppers()
    local on = Enabled()
    local previousRadio, nextRadio = dd:CollectSelectionData()
    dec:SetEnabled(on and previousRadio ~= nil)
    inc:SetEnabled(on and nextRadio ~= nil)
  end

  dec:SetScript("OnClick", function() dd:Decrement(); UpdateSteppers(); PlaySound(SOUNDKIT.IG_MAINMENU_OPTION_CHECKBOX_ON) end)
  inc:SetScript("OnClick", function() dd:Increment(); UpdateSteppers(); PlaySound(SOUNDKIT.IG_MAINMENU_OPTION_CHECKBOX_ON) end)

  dd:RegisterCallback(DropdownButtonMixin.Event.OnUpdate, UpdateSteppers, dd)

  -- An optional precondition can disable the control (the resolution dropdown while the
  -- game window is maximised, where a pick has no effect). SetEnabled(false) greys it and
  -- blocks the menu; a disabled widget normally stops firing OnEnter, so keep its motion
  -- scripts alive (as Blizzard's own disabled controls do) to still explain why on hover.
  if enableWhen then
    row.hasEnableWhen = true
    dd:SetMotionScriptsWhileDisabled(true)
    if disabledTooltip then
      dd:HookScript("OnEnter", function(self)
        if Enabled() then return end
        GameTooltip:SetOwner(self, "ANCHOR_RIGHT")
        GameTooltip_SetTitle(GameTooltip, row.label:GetText())
        GameTooltip_AddNormalLine(GameTooltip, disabledTooltip, true)
        GameTooltip:Show()
      end)
      dd:HookScript("OnLeave", GameTooltip_Hide)
    end
  end

  -- Re-render the shown selection from the binding: re-runs the generator so the
  -- isSelected predicates re-read it (and the selectionFunc re-applies the markers),
  -- and refreshes the steppers via OnUpdate. A disabling precondition is re-checked too.
  row.Refresh = function()
    if enableWhen then
      local on = enableWhen()
      dd:SetEnabled(on)
      row.controlDisabled = not on
    end
    dd:GenerateMenu()  -- re-runs SetSelectionText, which greys the text while disabled
  end
  if enableWhen then row.Refresh() end  -- apply the initial enabled/disabled state
end

-- A checkbox bound to a 0/1 CVar -- used only via an explicit "checkbox" control (a
-- descriptor override or a custom setting), never as a default. Same contract as the
-- slider/dropdown: reads binding.get(), writes binding.set(0/1), and exposes
-- row.Refresh to re-render from the binding without echoing back.
local function CreateCheckbox(row, binding, enableWhen)
  local check = CreateFrame("CheckButton", nil, row, "UICheckButtonTemplate")
  check:SetSize(26, 26)
  check:SetPoint("LEFT", row.label, "RIGHT", 8, 0)
  check:SetScript("OnClick", function(self)
    local on = self:GetChecked()
    PlaySound(on and SOUNDKIT.IG_MAINMENU_OPTION_CHECKBOX_ON or SOUNDKIT.IG_MAINMENU_OPTION_CHECKBOX_OFF)
    binding.set(on and 1 or 0)
  end)
  row.hasEnableWhen = enableWhen ~= nil  -- so a Graphit edit can refresh it at once
  row.Refresh = function()
    check:SetChecked(tonumber(binding.get()) == 1)
    -- An optional precondition: while it is false the control is greyed and inert (e.g.
    -- the MSAA alpha test is dead while Multisample AA is None). Re-checked on every
    -- refresh, so a change to the governing CVar -- even from the Blizzard panel, via the
    -- inward sync -- updates it; the label greys through row.controlDisabled.
    if enableWhen then
      local enabled = enableWhen() and true or false
      check:SetEnabled(enabled)
      row.controlDisabled = not enabled
    end
  end
  row.Refresh()
end

-- Unique per-level sample values of a child entry, sorted ascending (strings).
local function DistinctChildValues(data)
  local seen, list = {}, {}
  for key, value in pairs(data) do
    if type(key) == "number" and not seen[value] then
      seen[value] = true
      list[#list + 1] = value
    end
  end
  table.sort(list, function(a, b) return (tonumber(a) or 0) < (tonumber(b) or 0) end)
  return list
end

-- The option list a control offers: a static `options`, or a runtime `optionsFunc`
-- resolved now. nil for an option-less control (a plain slider, checkbox, or CVar-less).
local function ResolveOptions(control)
  return control and (control.options or (control.optionsFunc and control.optionsFunc()))
end

-- A dropdown control defaults to a slider over its option values, keeping the labels
-- for the readout (CreateSlider's spec.labels); any other control (an explicit slider
-- or checkbox, e.g. on a standalone Layer-1 setting) is returned unchanged.
local function SliderFromControl(control)
  local options = ResolveOptions(control)
  if not options then return control end  -- a plain numeric slider / checkbox passes through
  local values, labels = {}, {}
  for _, o in ipairs(options) do
    values[#values + 1] = o.value
    labels[o.value] = L(o.label)
  end
  table.sort(values)
  return { kind = "slider", values = values, labels = labels, betterIsLower = control.betterIsLower }
end

-- A setting with only one possible option has nothing to choose: show that single label
-- as static text where the control would be (e.g. Multisample AA = "None" on hardware
-- with no MSAA support), rather than a one-position slider or dropdown.
local function CreateStaticReadout(row, text)
  -- Centre it across the control area (where a dropdown would sit), in the same
  -- GameFontNormal the slider readouts use, so it reads like a normal value.
  local fs = row:CreateFontString(nil, "ARTWORK", "GameFontNormal")
  fs:SetPoint("LEFT", row.label, "RIGHT", 8, 0)
  fs:SetPoint("RIGHT", row, "RIGHT", -8, 0)
  fs:SetJustifyH("CENTER")
  fs:SetText(text or "")
  fs:SetTextColor(row.labelColor:GetRGB())
end

-- Merge a descriptor `control` override over a base control; override fields win.
local function MergedControl(base, override)
  if not override then return base end
  local merged = {}
  for k, v in pairs(base) do merged[k] = v end
  for k, v in pairs(override) do merged[k] = v end
  return merged
end

-- The descriptor's override entry for a CVar (Layer 2 or Layer 3), or nil. Holds
-- an optional `control` table, a `hidden` flag, a `deferApply` flag, and a
-- `tooltip` (overrides the Layer-1/2 tooltip; the only source for a Layer-3 one).
local function OverrideFor(cvar)
  local d = Graphit.descriptor
  return d and d.overrides and d.overrides[cvar]
end

-- ===== Deferred apply =====
--
-- Some CVars freeze the game for seconds when changed (worldBaseMip). A direct
-- edit of such a child accepts the freeze, but a change reached indirectly -- by
-- driving its Layer-2 parent or the Layer-1 master -- is held back instead: its
-- target is parked in `pending` and the user commits it later via the Apply
-- button. A deferred CVar is "pending" exactly while its target differs from its
-- live value, so driving a parent back to the value it had cancels the entry.
-- A standalone Layer-1 setting marked deferApply (uiscale, whose live write rescales
-- the UI and slides the frame mid-drag) parks its own direct edits the same way.
--
-- The same `pending` + Apply button also carries standalone Layer-1 settings whose
-- commit is "gxRestart" (Compatibility toggles, Graphics API / Card, Triple Buffering):
-- those stage directly on edit, and the commit runs RestartGx (see CreateRow's binding
-- and ApplyPending).
--
-- The rest of the UI reads each child's LOGICAL value (the pending target if any,
-- else the live CVar), so a parent and its deferred child stay visually in step;
-- only the Apply button discloses that the change is not yet really applied.
local pending = {}  -- [cvar] = target value (string); absent when target == live

-- Forward declaration: assigned in BuildFrame once the Apply button exists.
local RefreshApplyButton

local function IsDeferred(cvar)
  local o = OverrideFor(cvar)
  return o and o.deferApply
end

local function LogicalValue(cvar)
  return pending[cvar] or GetCVar(cvar)
end

-- Park (or clear) a deferred child's target. It is pending only while different
-- from the live CVar, so a target equal to reality removes any existing entry.
local function SetPending(cvar, target)
  if tostring(target) == tostring(GetCVar(cvar)) then
    pending[cvar] = nil
  else
    pending[cvar] = tostring(target)
  end
end

-- A clientRestart setting is written live but only takes effect after a full game
-- restart, so it is "pending restart" (tinted violet) exactly while its live value
-- differs from the value the running client loaded at startup. We capture those active
-- values once per client session and persist them, keyed by the client's start time
-- (wall clock now minus GetSessionTime, the uptime). That start time is stable across a
-- /reload and a character relog (same process) and jumps by minutes on a real restart.
-- time() ticks on wall-clock seconds while GetSessionTime() ticks on session-relative
-- ones, so their difference jitters by ~a second -- hence the leeway (far below a real
-- restart): within it we treat the token as the same session and preserve the baseline.
local RESTART_SESSION_LEEWAY = 10  -- seconds; absorbs the timer jitter / minor clock drift

local function EnsureRestartBaseline()
  Graphit_config = Graphit_config or {}
  local started = time() - GetSessionTime()
  local b = Graphit_config.restartBaseline
  if not (b and b.started) or math.abs(b.started - started) > RESTART_SESSION_LEEWAY then
    local values = {}
    for _, s in ipairs(Graphit.layer1 or {}) do
      if s.commit == "clientRestart" and s.cvar then
        values[s.cvar] = tostring(GetCVar(s.cvar))
      end
    end
    Graphit_config.restartBaseline = { started = started, values = values }
  end
end

local function IsRestartPending(cvar)
  local b = Graphit_config and Graphit_config.restartBaseline
  return b and b.values[cvar] ~= nil and tostring(GetCVar(cvar)) ~= b.values[cvar]
end

-- The commit flag a standalone Layer-1 setting declares (built once from the layout),
-- or nil. ApplyPending reads it to fire a graphics-device restart when committing a
-- staged "gxRestart" setting.
local commitOf
local function CommitFlag(cvar)
  if not commitOf then
    commitOf = {}
    for _, s in ipairs(Graphit.layer1 or {}) do
      if s.cvar and s.commit then commitOf[s.cvar] = s.commit end
    end
  end
  return commitOf[cvar]
end

-- ===== Parent level reconciliation =====

-- The sorted list of quality levels a meta defines (children share the set).
local function MetaLevels(matrix)
  local set = {}
  for _, data in pairs(matrix) do
    for k in pairs(data) do
      if type(k) == "number" then set[k] = true end
    end
    break
  end
  local levels = {}
  for lvl in pairs(set) do levels[#levels + 1] = lvl end
  table.sort(levels)
  return levels
end

-- Resolve the current child CVars to a parent level plus markers: a trailing "+"
-- when the defining children sit between levels (above the shown one), a trailing
-- "-" when one falls below even the lowest level's preset, and a leading "~" when
-- a "free" child is off this level's preset.
--
-- Only "defining" children -- those whose presets are monotonic across the
-- levels -- determine the level. A constant or non-monotonic child (e.g.
-- lodObjectMinSize, presets 0..0, 30, 20, where a higher parent level does not
-- mean a better child) has no coherent level: it cascades down from the parent
-- but never sets the level, only flagging it approximate ("~") when off-preset.
--
-- Among the defining children, an exact full match wins (highest level first);
-- otherwise the level is the highest they all reach (the weakest child's reach,
-- in each child's own quality direction), clamped to the lowest level -- the "-"
-- case is when that reach falls below the lowest.
--
-- `valueOf(childCvar)` reads a child's current value; it defaults to the logical
-- (pending-aware) CVar value. The master passes its own getter so it reconciles
-- from each meta's resolved level rather than the meta CVar, which a deferred
-- drive leaves stale.
local function HighestSatisfiedLevel(meta, valueOf)
  valueOf = valueOf or LogicalValue
  local matrix = Graphit.layer3[meta]
  local levels = MetaLevels(matrix)

  local defining, free = {}, {}
  for childCvar, data in pairs(matrix) do
    local dir, monotonic = 0, true
    local prev = tonumber(data[levels[1]])
    for i = 2, #levels do
      local cur = tonumber(data[levels[i]])
      if cur > prev and dir < 0 then monotonic = false; break
      elseif cur < prev and dir > 0 then monotonic = false; break
      elseif cur > prev then dir = 1
      elseif cur < prev then dir = -1 end
      prev = cur
    end
    if monotonic and dir ~= 0 then
      defining[#defining + 1] = { cvar = childCvar, data = data, increasing = dir > 0 }
    else
      free[#free + 1] = { cvar = childCvar, data = data }
    end
  end

  local level, exact, below = nil, nil, false
  for i = #levels, 1, -1 do
    local lvl, matchAll = levels[i], true
    for _, c in ipairs(defining) do
      if tonumber(valueOf(c.cvar)) ~= tonumber(c.data[lvl]) then
        matchAll = false
        break
      end
    end
    if matchAll then level, exact = lvl, true; break end
  end

  if not level then
    local minSat
    for _, c in ipairs(defining) do
      local v = tonumber(valueOf(c.cvar))
      local sat = levels[1] - 1
      for _, lvl in ipairs(levels) do
        local target = tonumber(c.data[lvl])
        local reached
        if c.increasing then reached = v >= target else reached = v <= target end
        if reached then sat = lvl else break end
      end
      if not minSat or sat < minSat then minSat = sat end
    end
    below = minSat ~= nil and minSat < levels[1]
    level, exact = math.max(minSat or levels[#levels], levels[1]), false
  end

  local approx = false
  for _, c in ipairs(free) do
    local target = c.data[level]
    if target ~= nil and tonumber(valueOf(c.cvar)) ~= tonumber(target) then
      approx = true
      break
    end
  end

  return level, exact, approx, below
end

-- Reconcile a parent's displayed level and markers from its children. The shown level
-- (row.level) is decoupled from the meta CVar: editing children never rewrites
-- the meta (which would cascade and wipe the customization) -- it only updates
-- what the parent shows. The meta is written solely when the user drives the
-- parent control.
--
-- The master additionally inherits its children's markers: if any Layer-2 row is
-- showing +/-/~, the master shows it too. This catches deviations its own
-- computation cannot see, since an L3 child edit deviates the Layer-2 row without
-- moving the meta CVar the master reconciles from. Callers therefore reconcile
-- the master after the Layer-2 row, so it reads the child's fresh markers.
local function ReconcileParent(parentRow)
  if not parentRow.Refresh then return end

  -- The master's children are the meta rows, so it reads each meta's resolved
  -- level (decoupled, and correct even when a deferred drive left the meta CVar
  -- stale); a plain single-child meta has no level, so fall back to its CVar.
  local valueOf
  if parentRow.isMaster then
    local content = parentRow:GetParent()
    valueOf = function(metaCvar)
      local r = content.rowByMeta and content.rowByMeta[metaCvar]
      if r and r.level ~= nil then return r.level end
      return LogicalValue(metaCvar)
    end
  end

  local level, exact, approx, below = HighestSatisfiedLevel(parentRow.meta, valueOf)
  local exceeds = not exact and not below

  if parentRow.isMaster then
    for _, r in ipairs(parentRow:GetParent().rows) do
      if r ~= parentRow then
        exceeds = exceeds or r.exceeds
        below   = below   or r.below
        approx  = approx  or r.approx
      end
    end
    -- "-" pins a row to its floor (a child fell below even the lowest preset), so
    -- inherit that floor too: an L2 reading "1-" makes the master read "1-", not
    -- the higher level its meta levels would otherwise map to.
    if below then level = MetaLevels(Graphit.layer3[parentRow.meta])[1] end
  end

  parentRow.level   = level
  parentRow.below   = below or false
  parentRow.exceeds = (exceeds and not below) or false  -- "-" takes precedence over "+"
  parentRow.approx  = approx or false
  parentRow.Refresh()
end

-- ===== Rows =====

-- A row's name label sits at row.labelColor normally; it turns WARNING_FONT_COLOR while it
-- (or a quality child) sits on a risky option (Particle Density = Disabled), or
-- ORANGE_FONT_COLOR while a change is pending under it. A warning (a present danger)
-- outranks pending for the label colour, though the tooltip shows both. See HighlightPending.
local CHILD_LABEL_COLOR = CreateColor(0.75, 0.75, 0.75)

local function SetRowLabelPending(row, isPending, isWarning, isRestart)
  row.pendingHighlight = isPending  -- both flags are read by the label tooltip for its note
  row.warningHighlight = isWarning
  row.restartHighlight = isRestart
  local color = (isWarning and WARNING_FONT_COLOR)
                or (isPending and ORANGE_FONT_COLOR)
                or (isRestart and RESTART_FONT_COLOR)
                or (row.controlDisabled and DISABLED_FONT_COLOR) or row.labelColor
  row.label:SetTextColor(color:GetRGB())
  if row.readout then row.readout:SetTextColor(color:GetRGB()) end  -- keep the slider value in step
end

-- A single Layer-3 child row: the raw CVar name and its control, indented under
-- the parent. `control` is the resolved spec (a slider by default, with any
-- descriptor override merged in); a slider's range and a dropdown's values default
-- from the dumped data when the control does not give them. Editing a child
-- reconciles the parent, snapping its level or marking it (+/-/~).
local function CreateChildRow(parent, childCvar, meta, parentRow, control)
  local row = CreateFrame("Frame", nil, parent)
  row:SetHeight(CHILD_ROW_HEIGHT)
  AddHover(row)

  row.label = row:CreateFontString(nil, "ARTWORK", "GameFontHighlightSmall")
  row.label:SetPoint("LEFT", CHILD_INDENT, 0)
  -- Full label width so the indent carries through to the control too (the
  -- control still anchors its right edge to the row, so its right stays put).
  row.label:SetWidth(L3_LABEL_WIDTH)
  row.label:SetJustifyH("LEFT")
  row.label:SetWordWrap(false)
  row.labelColor = CHILD_LABEL_COLOR
  row.label:SetTextColor(row.labelColor:GetRGB())
  row.label:SetText(childCvar)
  row.cvar = childCvar  -- so HighlightPending can test pending[row.cvar]

  -- Layer-3 has no Blizzard tooltip; show one only if the descriptor sets it.
  local override = OverrideFor(childCvar)
  AddLabelTooltip(row, childCvar, L(override and override.tooltip))

  local data = Graphit.layer3[meta][childCvar]

  -- A child shows its logical (pending-aware) value and a direct edit applies
  -- immediately -- accepting the freeze for a deferred CVar -- which also clears
  -- any parked target. Then it reconciles the parent and the master a tier up.
  local binding = {
    get = function() return LogicalValue(childCvar) end,
    set = function(v)
      SetCVar(childCvar, tostring(v))
      pending[childCvar] = nil
      if parentRow then
        ReconcileParent(parentRow)
        local content = parentRow:GetParent()
        if content.masterRow then ReconcileParent(content.masterRow) end
      end
      if RefreshApplyButton then RefreshApplyButton() end
    end,
  }

  if control.kind == "slider" then
    CreateSlider(row, binding, {
      min  = control.min  or tonumber(data.min),
      max  = control.max  or tonumber(data.max),
      step = control.step or 1,
      betterIsLower = control.betterIsLower,
      labels = control.labels,
    })
  elseif control.kind == "dropdown" then
    local values = control.values or DistinctChildValues(data)
    local opts = {}
    for _, v in ipairs(values) do opts[#opts + 1] = { value = v, text = tostring(v) } end
    CreateDropdown(row, binding, opts, control.betterIsLower)
  elseif control.kind == "checkbox" then
    CreateCheckbox(row, binding)
  end
  return row
end

-- Order a meta's child CVars for display: the ones named in `order` come first,
-- in that order; any the list omits (e.g. a child added in a later patch) follow,
-- sorted alphabetically so nothing is silently dropped. With no order, all
-- children are alphabetical.
local function OrderedChildNames(childData, order)
  local rank = {}
  if order then
    for i, cvar in ipairs(order) do rank[cvar] = i end
  end
  local names = {}
  for cvar in pairs(childData) do names[#names + 1] = cvar end
  table.sort(names, function(a, b)
    local ra, rb = rank[a], rank[b]
    if ra and rb then return ra < rb end
    if ra ~= rb then return ra ~= nil end  -- listed children sort before unlisted
    return a < b
  end)
  return names
end

-- Lazily build the indented container of child rows for an expandable row. The
-- children come from the L3 dump, ordered by the descriptor's childOrder for this
-- meta (row.childOrder), each with its descriptor control override merged in. A
-- child with a `hidden` override is skipped here, but its CVar still cascades and
-- still counts toward the parent's level.
local function BuildChildren(row)
  if row.childContainer then return end
  local content   = row:GetParent()
  local container = CreateFrame("Frame", nil, content)
  local childData = Graphit.layer3[row.meta]
  local names     = OrderedChildNames(childData, row.childOrder)

  container.childRows = {}
  local y = 0
  for _, cvar in ipairs(names) do
    local override = OverrideFor(cvar)
    if not (override and override.hidden) then
      local control  = MergedControl({ kind = "slider" }, override and override.control)
      local childRow = CreateChildRow(container, cvar, row.meta, row, control)
      childRow:SetPoint("TOPLEFT", container, "TOPLEFT", 0, y)
      childRow:SetPoint("RIGHT", container, "RIGHT", 0, 0)
      container.childRows[#container.childRows + 1] = childRow
      y = y - CHILD_ROW_HEIGHT
    end
  end
  container:SetHeight(-y)
  container:Hide()
  row.childContainer = container
end

-- Stack rows top-down, then size the scroll content and refresh the scroll range.
-- The master leads flush, and any custom Layer-1 row sits flush too and always shows;
-- the Layer-2 rows between them show only while the master is expanded, indented under
-- it, each with its own expanded Layer-3 container (indented to match). With no master,
-- all rows show flush.
local function Relayout(content)
  local y = -4
  local master  = content.masterRow
  local l2Shown = (not master) or master.expanded
  local indent  = master and L2_INDENT or 0
  for _, row in ipairs(content.rows) do
    if row.isMaster or row.topLevel or row.isHeader then
      -- The master, any standalone Layer-1 setting, and the section headers are flush
      -- and always shown, outside the master's collapse group. Headers are taller, so
      -- advance by the row's own height.
      row:ClearAllPoints()
      row:SetPoint("TOPLEFT", content, "TOPLEFT", 0, y)
      row:SetPoint("RIGHT", content, "RIGHT", 0, 0)
      row:Show()
      y = y - row:GetHeight()
    elseif l2Shown then
      row:ClearAllPoints()
      row:SetPoint("TOPLEFT", content, "TOPLEFT", indent, y)
      row:SetPoint("RIGHT", content, "RIGHT", 0, 0)
      row:Show()
      y = y - ROW_HEIGHT
      if row.childContainer then
        if row.expanded then
          row.childContainer:ClearAllPoints()
          row.childContainer:SetPoint("TOPLEFT", content, "TOPLEFT", indent, y)
          row.childContainer:SetPoint("RIGHT", content, "RIGHT", 0, 0)
          row.childContainer:Show()
          y = y - row.childContainer:GetHeight()
        else
          row.childContainer:Hide()
        end
      end
    else
      row:Hide()
      if row.childContainer then row.childContainer:Hide() end
    end
  end
  content:SetHeight(-y + 4)
  if content.scrollBox and content.scrollBox:GetView() then
    content.scrollBox:FullUpdate()
  end
end

-- Point an expander button's icon at the open or closed chevron.
local function SetExpanderAtlas(btn, expanded)
  if expanded then
    btn:SetNormalAtlas("common-button-dropdown-open")
    btn:SetPushedAtlas("common-button-dropdown-openpressed")
  else
    btn:SetNormalAtlas("common-button-dropdown-closed")
    btn:SetPushedAtlas("common-button-dropdown-closedpressed")
  end
end

-- Persist a row's expanded state by CVar in the SavedVariable.
local function SetExpanded(cvar, value)
  Graphit_config = Graphit_config or {}
  Graphit_config.expanded = Graphit_config.expanded or {}
  Graphit_config.expanded[cvar] = value
end

-- Toggle a row's expander and swap its +/- icon. A Layer-2 row reveals its built
-- Layer-3 container; the master reveals the Layer-2 rows (which already exist, so
-- it skips BuildChildren) -- Relayout shows or hides them either way.
local function ToggleExpand(row)
  if not row.isMaster then BuildChildren(row) end
  row.expanded = not row.expanded
  SetExpanded(row.meta, row.expanded)
  SetExpanderAtlas(row.expander, row.expanded)
  Relayout(row:GetParent())
  -- Tint any freshly built child rows that are already pending.
  if RefreshApplyButton then RefreshApplyButton() end
end

-- Build a row's expand/collapse button, its icon reflecting the initial state.
local function CreateExpander(row)
  local btn = CreateFrame("Button", nil, row)
  btn:SetSize(16, 16)
  btn:SetPoint("LEFT", 2, 0)
  btn:SetHighlightTexture("Interface\\Buttons\\UI-PlusButton-Hilight")
  btn:SetScript("OnClick", function() ToggleExpand(row) end)
  SetExpanderAtlas(btn, row.expanded)
  row.expander = btn
end

-- A non-interactive, greyed-out expander icon for a childless Layer-2 row, purely
-- to keep its name column aligned with the expandable rows around it. A plain
-- texture (no Button) so it takes no mouse and the row's hover still reads through.
local function CreatePlaceholderExpander(row)
  local tex = row:CreateTexture(nil, "ARTWORK")
  tex:SetSize(16, 16)
  tex:SetPoint("LEFT", 2, 0)
  tex:SetAtlas("common-button-dropdown-open")
  tex:SetDesaturated(true)
