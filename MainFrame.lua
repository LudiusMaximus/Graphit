-- =====================================================================
-- Graphit main window.
--
-- A flat panel built like the Blizzard Settings frame (nine-slice border + flat
-- background, no template portrait): movable by a title-bar strip, resizable by a bottom-right
-- grip, clamped to the screen, with live FPS in a masked circle in its top-right corner.
-- The body is a scrolling list of Blizzard's graphics settings, built
-- from four data files joined by CVar name:
--   * Settings_Source.lua      the user-facing settings, mirrored from Blizzard: the
--                          Graphics Quality group -- the preset and its secondaries
--                          (Data-Layer 2, Graphit.layer2) -- plus the top-level section
--                          layout and the standalone display / advanced / compat settings
--                          (Data-Layer 1, Graphit.layer1).
--   * Settings_Dump.lua      for each multi-tertiary secondary, its raw tertiary CVars and
--                          their value per level (Data-Layer 3, Graphit.layer3).
--   * Settings_Custom.lua  our declarative overlay: display order, tab assignment, and
--                          per-setting / per-tertiary overrides (control swaps, hidden, deferApply).
--   * Settings_Logic.lua   our behavioural overlay: per-setting closures (enableWhen, get /
--                          set, option hints / warnings) merged onto the mirror by CVar or name.
-- The first is a faithful transcription (re-done from Blizzard's source on patch day), the second
-- is auto-generated by the dump tool; neither carries our decisions -- those live in the last two
-- (Settings_Custom declarative, Settings_Logic behavioural). (For the primary / secondary /
-- tertiary vocabulary and the Data-Layer axis, see "Row layout" below.) A secondary gets a "+"
-- expander (revealing its tertiaries) exactly when it has an entry in the layer3 data; the preset
-- always expands, revealing its secondaries. Every control defaults to a slider (a Blizzard
-- dropdown keeps its labels on the slider readout); the descriptor overrides that where a
-- dropdown or other control reads better.
--
-- Every control goes through a small {get, set} binding so sliders and dropdowns share one
-- implementation. A plain setting binds to its own CVar; an expandable row (the preset, or a
-- secondary with tertiaries) binds instead to a computed quality level (row.level).
--
-- A secondary and its tertiaries stay in sync both ways:
--   * Down: driving the secondary writes its CVar; the engine cascades that to the raw tertiary
--     CVars after a short delay (CASCADE_SETTLE), after which the tertiary controls refresh.
--   * Up: editing a tertiary reconciles its secondary (ReconcileParent). The level is the highest
--     the monotonic tertiaries still satisfy, with a trailing "+" when one runs past it or "-"
--     when one drops below the lowest level. A non-monotonic "free" tertiary (no coherent level)
--     cascades down but never sets the level; off its preset value it only adds a leading "~".
--     Editing a tertiary never rewrites the secondary's CVar -- that would cascade and wipe the
--     customization -- so the secondary's shown level is decoupled from its CVar.
--
-- The same two-way sync repeats one tier up, between the preset (graphicsQuality) and its
-- secondaries: driving the preset sets every secondary (it has no SetCVar cascade of its own, so
-- MainFrame writes each secondary from the layer3 matrix); editing any secondary or standalone
-- setting reconciles the preset's level with the same +/-/~ markers. The preset also inherits its
-- secondaries' markers, so a tertiary edit that deviates a secondary (without moving the
-- secondary's CVar) still surfaces on the preset.
--
-- A tertiary marked deferApply (Settings_Custom) freezes the game when changed, so a change
-- reached through its secondary or the preset is parked in `pending` rather than applied, and
-- committed via the glowing Apply button -- or, since `pending` persists in Graphit_config,
-- automatically on the next loading screen (any zone, reload, or login), where the freeze rides
-- hidden. The UI reads such a tertiary's logical (pending-aware) value so its secondary stays in
-- step; see "Deferred apply" and "Persistence".
--
-- Graphit and the Settings panel act on the same CVars, so they stay in sync while
-- both are open. Inward: Graphit watches the CVars it displays (CVar callbacks, plus a
-- few window / device events for the CVar-less ones) and refreshes. Outward: a Graphit
-- edit rebuilds the panel's graphics category --
-- Blizzard's graphics settings are proxy settings that do not refresh from an
-- external CVar write on their own. See "Live sync with the Settings panel".
--
-- The list background is a nine-slice cut from Blizzard's options.blp so the
-- window reads as a small sibling of the in-game Settings panel. Toggle with
-- /graphit.
-- =====================================================================

