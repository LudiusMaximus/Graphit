-- =====================================================================
-- Graphit main window.
--
-- A flat PortraitFrame: movable by a title-bar strip, resizable by a
-- bottom-right grip, clamped to the screen, with live FPS in the portrait
-- circle. The body is a scrolling list of Blizzard's graphics settings, built
-- from three data files joined by CVar name:
--   * Settings_Source.lua      the user-facing settings, mirrored from Blizzard: the
--                          Graphics Quality group and its parent controls (Layer 2,
--                          Graphit.layer2), plus the top-level section layout and the
--                          standalone display / advanced / compat settings
--                          (Graphit.layer1).
--   * Settings_Dump.lua      for each multi-child meta, the raw child CVars and
--                          their value per level (Layer 3).
--   * Settings_Custom.lua  our overlay: display order plus per-setting and
--                          per-child overrides.
-- The first is a faithful transcription (re-done from Blizzard's source on patch day),
-- the second is auto-generated by the dump tool; neither carries our decisions -- those
-- live in the third. A setting is expandable (gets a "+" expander that reveals its
-- children) exactly when it has an entry in the L3 data. Every control defaults to
-- a slider (a Blizzard dropdown keeps its labels on the slider readout); the
-- descriptor overrides that where a dropdown or other control reads better.
--
-- Every control goes through a small {get, set} binding so sliders and
-- dropdowns share one implementation. A plain setting binds to its own CVar;
-- an expandable parent binds instead to a computed quality level (row.level).
--
-- A parent and its children stay in sync both ways:
--   * Down: driving a parent writes its meta CVar; the engine cascades that to
--     the raw child CVars after a short delay (CASCADE_SETTLE), after which the
--     child controls refresh.
--   * Up: editing a child reconciles the parent (ReconcileParent). The level is
--     the highest the monotonic children still satisfy, with a trailing "+" when
--     one runs past it or "-" when one drops below the lowest level. A
--     non-monotonic "free" child (no coherent level) cascades down but never sets
--     the level; off its preset it only adds a leading "~". Editing a child never
--     rewrites the meta CVar -- that would cascade and wipe the customization --
--     so the parent's shown level is decoupled from the meta.
--
-- The same two-way sync repeats one tier up. The leading row is the Layer-1
-- master (graphicsQuality), whose "children" are the meta CVars: driving it sets
-- every meta (it has no SetCVar cascade of its own, so MainFrame writes each
-- meta from the L3 matrix); editing any meta or plain setting reconciles the
-- master's level with the same +/-/~ markers. The master also inherits its
-- children's markers, so an L3 edit that deviates a Layer-2 row (without moving
-- the meta CVar) still surfaces on the master.
--
-- A child CVar marked deferApply (Settings_Custom) freezes the game when changed,
-- so a change reached through its parent or the master is parked in `pending`
-- rather than applied, and committed via the glowing Apply button -- or, since
-- `pending` persists in Graphit_config, automatically on the next loading screen
-- (any zone, reload, or login), where the freeze rides hidden. The UI reads such a
-- child's logical (pending-aware) value so its parent stays in step; see "Deferred
-- apply" and "Persistence".
--
-- Graphit and the Settings panel act on the same CVars, so they stay in sync while
-- both are open. Inward: Graphit watches the CVars it displays (CVar callbacks, plus a
-- few window / device events for the CVar-less ones) and refreshes. Outward: a Graphit
-- edit rebuilds the panel's graphics category --
-- Blizzard's graphics settings are proxy settings that do not refresh from an
-- external CVar write on their own. See "Live sync with the Settings panel".
--
-- The list background is a nine-slice cut from Blizzard's options.blp so the
-- window reads as a small sibling of the in-game Settings panel. Toggle with
-- /graphit.
-- =====================================================================

