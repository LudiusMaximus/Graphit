-- ===== Persistence =====
--
-- Graphit_config is the SavedVariable (declared in the TOC): a table of state to
-- persist across sessions. It carries `pending`, the deferred changes a session
-- parked but never applied (see "Deferred apply"); `expanded`, the per-CVar
-- expand/collapse state of the master and Layer-2 rows (RestoreExpandedStates);
-- `geometry`, the frame's size and position (SaveGeometry / RestoreGeometry); and
-- `minimap`, the LibDBIcon button's position and hidden state (MinimapButton.lua).
--
-- Every loading screen is a chance to flush parked changes under cover of its
-- freeze, so we commit on PLAYER_ENTERING_WORLD (login, reload, and every zone).
-- We flush the whole `pending` set: each target still differing from its live
-- value is applied, each entry is then dropped, so nothing stays pending after.
--
-- `pending` persists by being the very table stored at Graphit_config.pending, so
-- on the first run we adopt last session's disk-saved parks into it and from then
-- on the two are one table -- the next logout simply writes whatever is left.
-- SavedVariables load only by ADDON_LOADED, so this must run on an event, not at
-- file scope; PLAYER_ENTERING_WORLD also lets the CVar writes land under the screen.
local function ResolveSavedPending()
  Graphit_config = Graphit_config or {}
  EnsureRestartBaseline()  -- re-captures only on a real client restart (start time jumped)

  -- First run: fold the disk-saved parks into the live table, then make the live
  -- table the persisted store (the same reference from here on).
  local saved = Graphit_config.pending
  if saved and saved ~= pending then
    for cvar, target in pairs(saved) do
      pending[cvar] = target
    end
  end
  Graphit_config.pending = pending

  -- Flush: apply each parked target that is not already met, and drop every entry
  -- (clearing the current key mid-traversal is allowed) so pending ends empty.
  for cvar, target in pairs(pending) do
    if tostring(GetCVar(cvar)) ~= tostring(target) then
      SetCVar(cvar, tostring(target))
    end
    pending[cvar] = nil
  end

  -- If the window was already built, re-sync it off the now-applied CVars and
  -- restore expanded states + geometry (the dev auto-open builds before the
  -- SavedVariable loads); otherwise the lazy first build reads them on its own.
  if frame and frame.content then
    RefreshAllRows(frame.content)
    RestoreExpandedStates(frame.content)
    Relayout(frame.content)
    frame.RestoreGeometry()
  end
end

local loader = CreateFrame("Frame")
loader:RegisterEvent("PLAYER_ENTERING_WORLD")
loader:SetScript("OnEvent", ResolveSavedPending)


