  -- Staged changes are parked rather than applied: a deferred child (worldBaseMip), to
  -- spare the multi-second freeze on a parent drag, and "gxRestart" Layer-1 settings
  -- whose device restart may be heavy. A round revert-icon button on the portrait's
  -- lower-right edge, glowing red while changes wait, commits them (RestartGx if any
  -- staged setting needs it).
  -- Parent to the PortraitContainer (and outrank it) so the button and its glow
  -- draw above the FPS portrait circle they overlap. The frame's NineSlice border
  -- (its TopLeftCorner laps over the portrait) sits higher still, so clear that too.
  local applyBtn = CreateFrame("Button", nil, f.PortraitContainer)
  applyBtn:SetSize(30, 30)
  local aboveLevel = f.PortraitContainer:GetFrameLevel()
  if f.NineSlice then aboveLevel = math.max(aboveLevel, f.NineSlice:GetFrameLevel()) end
  applyBtn:SetFrameLevel(aboveLevel + 10)
  -- Tunable: nudge to taste; sits on the bottom-right edge of the FPS portrait.
  applyBtn:SetPoint("CENTER", f.PortraitContainer.portrait, "BOTTOMRIGHT", 0, 12)

