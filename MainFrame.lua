-- Re-display the Settings panel's graphics category after a Graphit edit, so its
-- controls reflect our CVar changes. The graphics settings are proxy settings the
-- panel does not refresh from external CVar writes, so we rebuild the shown
-- category -- exactly what switching away and back does. Scoped to the graphics
-- category so a sensitive one (e.g. keybindings) is never rebuilt and tainted;
-- coalesced to once per frame; a no-op unless that category is currently open.
local settingsRefreshPending = false
local function RefreshSettingsPanel()
  if settingsRefreshPending or not (SettingsPanel and SettingsPanel:IsShown()) then return end
  settingsRefreshPending = true
  C_Timer.After(0, function()
    settingsRefreshPending = false
    if not (SettingsPanel and SettingsPanel:IsShown()) then return end
    -- Match by name: Settings.GetCategory takes a category ID, not a name, so it
    -- can't look the graphics category up by GRAPHICS_LABEL; the category's GetName
    -- is the label it was registered with.
    local current = SettingsPanel:GetCurrentCategory()
    if current and current:GetName() == GRAPHICS_LABEL then
      -- DisplayCategory rebuilds the list from scratch, which resets its scroll to the
      -- top. The content extent is unchanged (same rows, only values differ), so
      -- capture the scroll position first and restore the same percentage after --
      -- instantly (NoScrollInterpolation) so there is no visible jump.
      local list = SettingsPanel:GetSettingsList()
      local scrollBox = list and list.ScrollBox
      local pct = scrollBox and scrollBox:GetScrollPercentage()
      SettingsPanel:DisplayCategory(current)
      if scrollBox then
        scrollBox:SetScrollPercentage(pct, ScrollBoxConstants.NoScrollInterpolation)
      end
    end
  end)
end

-- Every Graphit CVar write goes through this local SetCVar, so each one also nudges
-- the Settings panel (the panel's own writes use its own SetCVar, not this one).
local function SetCVar(cvar, value)
  RawSetCVar(cvar, value)
  RefreshSettingsPanel()
end

