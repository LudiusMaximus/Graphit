  f.TitleText = f.NineSlice:CreateFontString(nil, "OVERLAY", "GameFontNormal")
  f.TitleText:SetPoint("TOP", 0, -5)
  f.TitleText:SetText("Graphit")

  -- Close button in the top-left corner (the FPS portrait + ring will claim the top right later).
  -- Plain Hide() -- a templated close button's default OnClick calls the secure HideUIPanel(), which
  -- is blocked in combat.
  f.CloseButton = CreateFrame("Button", nil, f, "UIPanelCloseButton")
  f.CloseButton:SetPoint("TOPLEFT", 3, 0)
  f.CloseButton:SetScript("OnClick", function(self) self:GetParent():Hide() end)

  -- Move via a drag strip across the title bar (stops short of the close button, now on the left).
  f:SetMovable(true)
  local dragBar = CreateFrame("Frame", nil, f)
  dragBar:SetPoint("TOPLEFT", 28, 0)
  dragBar:SetPoint("TOPRIGHT", 0, 0)
  dragBar:SetHeight(28)
  dragBar:EnableMouse(true)
  -- Move on mouse-down (not OnDragStart, which only fires after the cursor
  -- travels a threshold distance) so the drag is immediate.
  dragBar:SetScript("OnMouseDown", function() f:StartMoving() end)
  dragBar:SetScript("OnMouseUp", function() f:StopMovingOrSizing(); SaveGeometry() end)

  -- Resize via a bottom-right grip.
  f:SetResizable(true)
  -- Floor the min size so the inner-frame 9-slice corners never overlap: the
  -- content region must fit two corners on each axis, plus the fixed chrome
  -- between the frame edge and that region (CHROME_W / CHROME_H).
  -- The max height tracks the current screen, so recompute it on show and when
  -- the resolution / UI scale changes (the bound is otherwise set just once).
  local function ApplyResizeBounds()
    if not f.SetResizeBounds then return end
    local minW = math.max(MIN_WIDTH, 2 * INNER_CORNER_W + CHROME_W)
    local minH = math.max(MIN_HEIGHT, 2 * INNER_CORNER_H + CHROME_H)
    local maxHeight = math.max(UIParent:GetHeight() * MAX_HEIGHT_FRACTION, minH)
    f:SetResizeBounds(minW, minH, MAX_WIDTH, maxHeight)
    -- SetResizeBounds only limits future drags; it does not pull the current size
    -- in. A first-load default, or a geometry WoW's layout cache restored outside
    -- these bounds (the reported sub-MIN_WIDTH width), would otherwise stick until
    -- the next manual resize -- so clamp the live size into range here too.
    f:SetWidth(math.max(minW, math.min(f:GetWidth(), MAX_WIDTH)))
    f:SetHeight(math.max(minH, math.min(f:GetHeight(), maxHeight)))
    return maxHeight
  end

  -- A display/UI-scale change does not retroactively move an open frame, so after
  -- refreshing the bounds (which also re-clamps the size) pull any off-screen edge
  -- back into view.
  local function RefreshBoundsAndClamp()
    if not ApplyResizeBounds() then return end
    local left, bottom = f:GetRect()
    if left then
      local screenW, screenH = UIParent:GetWidth(), UIParent:GetHeight()
      left   = math.max(0, math.min(left,   screenW - f:GetWidth()))
      bottom = math.max(0, math.min(bottom, screenH - f:GetHeight()))
      f:ClearAllPoints()
      f:SetPoint("BOTTOMLEFT", UIParent, "BOTTOMLEFT", left, bottom)
    end
  end

  -- Restore the saved size and position (then clamp). Exposed on the frame so the
  -- saved-pending loader can re-apply once the SavedVariable has loaded -- the dev
  -- auto-open builds the frame before that. No saved geometry => the default stands.
  local function RestoreGeometry()
    local g = Graphit_config and Graphit_config.geometry
    if g and g.point then
      f:ClearAllPoints()
      f:SetPoint(g.point, UIParent, g.relPoint or g.point, g.x or 0, g.y or 0)
      f:SetSize(g.width or f:GetWidth(), g.height or f:GetHeight())
    end
    RefreshBoundsAndClamp()
  end
  f.RestoreGeometry = RestoreGeometry

  RestoreGeometry()
  f:HookScript("OnShow", ApplyResizeBounds)
  f:RegisterEvent("DISPLAY_SIZE_CHANGED")
  f:RegisterEvent("UI_SCALE_CHANGED")
  f:HookScript("OnEvent", RefreshBoundsAndClamp)
  local grip = CreateFrame("Button", nil, f)
  grip:SetSize(16, 16)
  grip:SetPoint("BOTTOMRIGHT", -4, 4)
  grip:SetNormalTexture("Interface\\ChatFrame\\UI-ChatIM-SizeGrabber-Up")
  grip:SetHighlightTexture("Interface\\ChatFrame\\UI-ChatIM-SizeGrabber-Highlight")
  grip:SetPushedTexture("Interface\\ChatFrame\\UI-ChatIM-SizeGrabber-Down")
  grip:SetScript("OnMouseDown", function() f:StartSizing("BOTTOMRIGHT") end)
  grip:SetScript("OnMouseUp", function() f:StopMovingOrSizing(); SaveGeometry() end)

  -- Live FPS in a hand-rolled masked circle with a metal ring, in the frame's top-right corner.
  -- PortraitContainer is our own frame: a dark square texture turned into a disc by Blizzard's
  -- circular alpha mask, framed by our circle.png ring, with the number over it.
  f.PortraitContainer = CreateFrame("Frame", nil, f)
  f.PortraitContainer:SetFrameLevel(f.NineSlice:GetFrameLevel() + 10)  -- above the border it overlaps at the corner
  f.PortraitContainer:SetSize(62, 62)
  f.PortraitContainer:SetPoint("TOPRIGHT", f, "TOPRIGHT", 8, 6.5)

  local portrait = f.PortraitContainer:CreateTexture(nil, "OVERLAY")
  f.PortraitContainer.portrait = portrait
  portrait:SetAllPoints(f.PortraitContainer)
  portrait:SetColorTexture(0.05, 0.05, 0.05, 1)
  local mask = f.PortraitContainer:CreateMaskTexture()
  mask:SetTexture("Interface\\CharacterFrame\\TempPortraitAlphaMask", "CLAMPTOBLACKADDITIVE", "CLAMPTOBLACKADDITIVE")
  mask:SetPoint("TOPLEFT", portrait, "TOPLEFT", 2, 0)
  mask:SetPoint("BOTTOMRIGHT", portrait, "BOTTOMRIGHT", -2, 4)
  portrait:AddMaskTexture(mask)

  -- Chrome ring framing the disc: our own circle.png in the addon folder (a photoshopped cut-out of
  -- Blizzard's portrait ring). The inner black disc gets fitted to it; centred on the
  -- container, above the fill (OVERLAY sublevel 2) but below the number (sublevel 7); not masked.
  local ring = f.PortraitContainer:CreateTexture(nil, "OVERLAY", nil, 2)
  f.PortraitContainer.ring = ring
  ring:SetTexture("Interface\\AddOns\\Graphit\\circle.png")
  ring:SetSize(64, 64)
  ring:SetPoint("CENTER", f.PortraitContainer, "CENTER")

  -- The FPS number, over the circle in a high OVERLAY sublevel so it draws above the fill.
  f.fpsText = f.PortraitContainer:CreateFontString(nil, "OVERLAY", "GameFontHighlightLarge2")
  f.fpsText:SetDrawLayer("OVERLAY", 7)
  f.fpsText:SetJustifyH("CENTER")          -- center on the current string, any digit count
  f.fpsText:SetPoint("CENTER", portrait, "CENTER", 0, -3)
  f.fpsText:SetTextColor(NORMAL_FONT_COLOR:GetRGB())

  -- "FPS" caption above the number, in a smaller font, also centered.
  f.fpsLabel = f.PortraitContainer:CreateFontString(nil, "OVERLAY", "GameFontHighlightSmall")
  f.fpsLabel:SetDrawLayer("OVERLAY", 7)
  f.fpsLabel:SetPoint("BOTTOM", f.fpsText, "TOP", 0, 0)
  f.fpsLabel:SetTextColor(NORMAL_FONT_COLOR:GetRGB())
  f.fpsLabel:SetText("FPS")

  -- Scrolling settings list (modern ScrollBox + scrollbar). No inset frame, so
  -- anchor to the frame directly: below the title bar, inside the borders.
  local scrollBox = CreateFrame("Frame", nil, f, "WowScrollBox")
  scrollBox:SetPoint("TOPLEFT", SCROLL_GAP_LEFT, -SCROLL_GAP_TOP)
  scrollBox:SetPoint("BOTTOMRIGHT", -(SCROLL_GAP_RIGHT + SCROLLBAR_CHANNEL), SCROLL_GAP_BOTTOM)

  -- Scrollbar centered in its channel, just right of the scroll box. Anchoring
  -- the top/bottom center points fixes the column and keeps the template width.
  local scrollBar = CreateFrame("EventFrame", nil, f, "MinimalScrollBar")
  scrollBar:SetPoint("TOP", scrollBox, "TOPRIGHT", SCROLLBAR_CHANNEL / 2 + SCROLLBAR_X, -SCROLLBAR_TOP)
  scrollBar:SetPoint("BOTTOM", scrollBox, "BOTTOMRIGHT", SCROLLBAR_CHANNEL / 2 + SCROLLBAR_X, SCROLLBAR_BOTTOM)

  -- The Settings inner-content frame, a nine-slice hand-cut from options.blp so
  -- the corners stay crisp and Blizzard's baked-in category divider is excluded.
  -- The nine pieces are TEXTURES on the frame, drawn behind the scroll box's
  -- tertiaries so they back the list without ever covering it. Corners are drawn
  -- at their native CW x CH; the edges and center stretch between them.
  --
  -- Heads-up on the pixel rects: SetTexCoord addresses the grid lines between
  -- pixels, not the pixels, so each coordinate is one more than the image
  -- editor's pixel index. Each corner is held to a clean 60x180 (1:3) by
  -- extending its inner edges (toward the center) by 1px, which only laps into
  -- the stretched border and stays invisible.
  local INNER_FILE = "Interface\\OptionsFrame\\Options"
  local TEX        = 1024
  local CW, CH     = INNER_CORNER_W, INNER_CORNER_H
  local PIECE = {
    TL = {1,   150, 61,  330}, TR = {828, 150, 888, 330},  -- corners (60x180)
    BL = {1,   589, 61,  769}, BR = {828, 589, 888, 769},
    T  = {101, 150, 120, 330}, B  = {101, 589, 120, 769},  -- clean border slices
    L  = {1,   330, 61,  589}, R  = {828, 330, 888, 589},  -- (divider-free)
    C  = {401, 401, 420, 420},                             -- solid center
  }

  -- Invisible region the nine-slice fills (frames the scroll box).
  local region = CreateFrame("Frame", nil, f)
  region:SetPoint("TOPLEFT", scrollBox, "TOPLEFT", -INNER_GAP_LEFT, INNER_GAP_TOP)
  -- Right side adds the scrollbar channel back so the border encloses it too.
  region:SetPoint("BOTTOMRIGHT", scrollBox, "BOTTOMRIGHT", SCROLLBAR_CHANNEL + INNER_GAP_RIGHT, -INNER_GAP_BOTTOM)

  local function piece(name)
    local t = f:CreateTexture(nil, "BACKGROUND")
    t:SetTexture(INNER_FILE)
    local r = PIECE[name]
    t:SetTexCoord(r[1] / TEX, r[3] / TEX, r[2] / TEX, r[4] / TEX)
    return t
  end
  local tl, tr, bl, br = piece("TL"), piece("TR"), piece("BL"), piece("BR")
  tl:SetSize(CW, CH); tl:SetPoint("TOPLEFT",     region, "TOPLEFT")
  tr:SetSize(CW, CH); tr:SetPoint("TOPRIGHT",    region, "TOPRIGHT")
  bl:SetSize(CW, CH); bl:SetPoint("BOTTOMLEFT",  region, "BOTTOMLEFT")
  br:SetSize(CW, CH); br:SetPoint("BOTTOMRIGHT", region, "BOTTOMRIGHT")

  local top = piece("T"); top:SetPoint("TOPLEFT", tl, "TOPRIGHT");    top:SetPoint("BOTTOMRIGHT", tr, "BOTTOMLEFT")
  local bot = piece("B"); bot:SetPoint("TOPLEFT", bl, "TOPRIGHT");    bot:SetPoint("BOTTOMRIGHT", br, "BOTTOMLEFT")
  local lft = piece("L"); lft:SetPoint("TOPLEFT", tl, "BOTTOMLEFT");  lft:SetPoint("BOTTOMRIGHT", bl, "TOPRIGHT")
  local rgt = piece("R"); rgt:SetPoint("TOPLEFT", tr, "BOTTOMLEFT");  rgt:SetPoint("BOTTOMRIGHT", br, "TOPRIGHT")
  local cen = piece("C"); cen:SetPoint("TOPLEFT", tl, "BOTTOMRIGHT"); cen:SetPoint("BOTTOMRIGHT", br, "TOPLEFT")

  -- The scroll tertiary. content.scrollable marks it as the ScrollBox's scroll
  -- target; content.scrollBox lets Relayout refresh the scroll range.
  local content = CreateFrame("Frame", nil, scrollBox)
  content.scrollable = true
  content.scrollBox = scrollBox
  -- Which tab's rows Relayout shows. Restored from the SavedVariable (the tab buttons save it
  -- below); first-ever open lands on "Static" (tab 1).
  content.activeTab = (Graphit_config and Graphit_config.activeTab) or 1
  f.content = content  -- so the saved-pending loader can re-sync rows after applying
  BuildRows(content)
  -- Keep the scroll tertiary width matched to the viewport so rows fill it.
  scrollBox:HookScript("OnSizeChanged", function() content:SetWidth(scrollBox:GetWidth()) end)

  ScrollUtil.InitScrollBoxWithScrollBar(scrollBox, scrollBar, CreateScrollBoxLinearView())
  scrollBar:SetHideIfUnscrollable(true)  -- hide the bar when the content fits (e.g. a short tab)
  -- Single-tertiary ScrollBox: the "pan extent" (per step / per wheel notch)
  -- defaults to the whole content frame, so a step would jump to min/max.
  -- Make a step one row instead.
  scrollBox:SetPanExtent(ROW_HEIGHT)
  scrollBox:FullUpdate()

  -- Persist the scroll position (0-1) live, so it survives a /reload; restored once on first show
  -- (the scroll range is only valid once the frame is laid out, hence the deferral below).
  scrollBox:RegisterCallback(BaseScrollBoxEvents.OnScroll, function(_, scrollPercentage)
    Graphit_config = Graphit_config or {}
    Graphit_config.scroll = scrollPercentage
  end, f)
  local scrollRestored = false
  f:HookScript("OnShow", function()
    if scrollRestored then return end
    scrollRestored = true
    local pct = Graphit_config and Graphit_config.scroll
    if pct then C_Timer.After(0, function() scrollBox:SetScrollPercentage(pct, true) end) end
  end)

  -- ===== Tabs =====
  -- Mirror the Settings panel's Game/AddOns tabs: MinimalTabTemplate buttons driven by a
  -- RadioButtonGroup, which gives the selected texture and the white (selected) vs gold
  -- (unselected) text for free; we add the tab click sound. Each row is tagged with its
  -- tab (BuildRows / the descriptor's tabGroups), so selecting a tab just sets
  -- content.activeTab and relays out. Placement is a first pass (bottoms on the list's
  -- top edge); tune freely.
  -- Tab underlay: a solid fill under each otherwise-transparent tab, so they read darker/less pale.
  -- Colour and opacity to taste (the per-state heights are just below the tab anchors).
  local function MakeTab(text)
    local tab = CreateFrame("Button", nil, f, "MinimalTabTemplate")
    -- Solid underlay at the lowest sublevel (behind the template's own art), anchored to the tab's
    -- bottom; its height and opacity are set per selection state (UpdateTabBackgrounds below).
    local bg = tab:CreateTexture(nil, "BACKGROUND", nil, -8)
    bg:SetColorTexture(0.0, 0.0, 0.0)  -- rgb; the per-state alpha is applied by UpdateTabBackgrounds
    bg:SetPoint("BOTTOMLEFT", tab, "BOTTOMLEFT", 2, 0)
    bg:SetPoint("BOTTOMRIGHT", tab, "BOTTOMRIGHT", -2, 0)
    tab.bg = bg
    tab.Text:SetText(text)
    return tab  -- width comes from the BOTTOMLEFT/BOTTOMRIGHT anchors below
  end
  -- Left tab is index 1, right tab is index 2,
  -- matching tabGroups; the indices are the visual left-to-right order.
  local tab1 = MakeTab("Static")
  local tab2 = MakeTab("Dynamic")
  local tab_y = 6
  local tab_gap = 5
  tab1:SetPoint("BOTTOMLEFT",  scrollBox, "TOPLEFT",  0,                 tab_y)
  tab1:SetPoint("BOTTOMRIGHT", scrollBox, "TOP",      -tab_gap/2,        tab_y)
  tab2:SetPoint("BOTTOMLEFT",  scrollBox, "TOP",       tab_gap/2,        tab_y)
  tab2:SetPoint("BOTTOMRIGHT", scrollBox, "TOPRIGHT", SCROLLBAR_CHANNEL, tab_y)

  -- The selected tab's underlay is a bit taller (its frame sits higher) and can be a different
  -- opacity than the unselected ones. Tweak these four; UpdateTabBackgrounds applies them to every
  -- tab on each selection change.
  local TAB_BG_HEIGHT_SELECTED   = 24
  local TAB_BG_HEIGHT_UNSELECTED = 21
  local TAB_BG_ALPHA_SELECTED    = 0.55
  local TAB_BG_ALPHA_UNSELECTED  = 0.3
  local tabs = { tab1, tab2 }
  local function UpdateTabBackgrounds(selectedIndex)
    for i, tab in ipairs(tabs) do
      local selected = (i == selectedIndex)
      tab.bg:SetHeight(selected and TAB_BG_HEIGHT_SELECTED or TAB_BG_HEIGHT_UNSELECTED)
      tab.bg:SetAlpha(selected and TAB_BG_ALPHA_SELECTED or TAB_BG_ALPHA_UNSELECTED)
    end
  end

  -- Select the restored tab BEFORE registering the callback, so the initial selection does not
  -- fire the click sound (the Settings panel orders it the same way).
  local tabsGroup = CreateRadioButtonGroup()
  tabsGroup:AddButtons({ tab1, tab2 })
  tabsGroup:SelectAtIndex(content.activeTab)
  UpdateTabBackgrounds(content.activeTab)  -- the initial select above does not fire the callback below
  -- The Selected event fires as (owner, tab, tabIndex); switch the list to that tab and save it.
  tabsGroup:RegisterCallback(ButtonGroupBaseMixin.Event.Selected, function(_, _, tabIndex)
    PlaySound(SOUNDKIT.IG_CHARACTER_INFO_TAB)
    UpdateTabBackgrounds(tabIndex)
    content.activeTab = tabIndex
    Relayout(content)
    Graphit_config = Graphit_config or {}
    Graphit_config.activeTab = tabIndex
  end, f)

  -- ===== Top section: always-visible display settings (Monitor / Display Mode / Resolution) =====
  -- Hardcoded layout (see IsTopSectionSetting): stacked, label-less dropdowns on the left,
  -- the tooltip moved onto the control. Data still comes from Settings_Source -- we pull the
  -- three out of layer1 in order and render them here; the list build above skips them.
  content.topRows = {}
  do
    local y = -TOP_SECTION_TOP
    for _, base in ipairs(Graphit.layer1) do
      if IsTopSectionSetting(base) then
        base = MergeLogic(base)  -- overlay Settings_Logic (Resolution's enableWhen / disabledTooltip)
        local row = CreateFrame("Frame", nil, f)
        row:SetHeight(TOP_ROW_HEIGHT)  -- width comes from the TOPLEFT/TOPRIGHT anchors
        row:SetPoint("TOPLEFT", f, "TOPLEFT", TOP_SECTION_LEFT, y)
        row:SetPoint("TOPRIGHT", f, "TOP", -5, y)
        row.label = row:CreateFontString(nil, "ARTWORK", "GameFontNormal")  -- zero-width: the control fills
        row.label:SetPoint("LEFT", row, "LEFT", 0, 0)
        row.labelColor = NORMAL_FONT_COLOR

        -- A windowUpdate binding: CVar-less (Resolution) reads/writes through closures; the
        -- others write their CVar. Either way refresh the window (a monitor / display change
        -- shifts what the other two read).
        local binding
        if base.read then
          binding = { get = base.read,
                      set = function(v) base.write(v); UpdateWindow(); RefreshAllRows(content) end }
        else
          binding = { get = function() return LogicalValue(base.cvar) end,
                      set = function(v) SetCVar(base.cvar, tostring(v)); UpdateWindow(); RefreshAllRows(content) end }
        end

        local opts = {}
        local raw = (base.control.optionsFunc and base.control.optionsFunc()) or base.control.options or {}
        for _, o in ipairs(raw) do opts[#opts + 1] = { value = o.value, text = Str(o.label) } end

        -- No label, so the control carries the tooltip: name + body + option detail.
        -- CreateDropdown appends the disabledTooltip ("locked") when Resolution is maximised.
        local detail = OptionDetailFunc(base)
        local function controlTooltip(tip)
          GameTooltip_SetTitle(tip, Str(base.name))
          local body = base.tooltip and _G[base.tooltip]
          if body then GameTooltip_AddNormalLine(tip, body, true) end
          if detail then detail(tip) end
        end

        CreateDropdown(row, binding, opts, base.control.betterIsLower, base.enableWhen,
                       base.disabledTooltip and Str(base.disabledTooltip), controlTooltip)
        row.Refresh()  -- initial render (list rows get this via OnShow's RefreshAllRows)
        content.topRows[#content.topRows + 1] = row
        y = y - (TOP_ROW_HEIGHT + TOP_ROW_GAP)
      end
    end
  end

  -- ===== Apply button =====
  -- Staged changes are parked rather than applied: a deferred tertiary (worldBaseMip), to
  -- spare the multi-second freeze on a parent drag, and "gxRestart" standalone settings
  -- whose device restart may be heavy. A round revert-icon button on the portrait's
  -- lower-left edge, glowing red while changes wait, commits them (RestartGx if any
  -- staged setting needs it).
  -- Parent to the PortraitContainer (and outrank it) so the button and its glow
  -- draw above the FPS portrait circle they overlap. The frame's NineSlice border sits
  -- higher still, and the portrait overlaps it at the corner, so clear that level too.
  local applyBtn = CreateFrame("Button", nil, f.PortraitContainer)
  applyBtn:SetSize(30, 30)
  local aboveLevel = f.PortraitContainer:GetFrameLevel()
  if f.NineSlice then aboveLevel = math.max(aboveLevel, f.NineSlice:GetFrameLevel()) end
  applyBtn:SetFrameLevel(aboveLevel + 10)
  -- Tunable: nudge to taste; sits on the bottom-left edge of the FPS portrait.
  applyBtn:SetPoint("BOTTOMLEFT", f.PortraitContainer.portrait, "BOTTOMLEFT", -7, -7)

  applyBtn:SetNormalAtlas(REVERT_ATLAS)
  applyBtn:SetPushedAtlas(REVERT_ATLAS)
  applyBtn:SetHighlightAtlas(REVERT_ATLAS)
  applyBtn:GetPushedTexture():SetVertexColor(0.7, 0.7, 0.7)  -- press feedback
  applyBtn:GetHighlightTexture():SetBlendMode("ADD")
  applyBtn:GetHighlightTexture():SetAlpha(0.3)

  -- Pulsating red glow (the WhoNeedsThisReagent minimap pattern): an additive
  -- highlight looping fade in/out while changes are pending.
  local glow = applyBtn:CreateTexture(nil, "OVERLAY")
  glow:SetAtlas("groupfinder-eye-highlight")
  glow:SetBlendMode("ADD")
  glow:SetVertexColor(1.0, 0.2, 0.2)
  glow:SetSize(applyBtn:GetWidth() * 1.1, applyBtn:GetHeight() * 1.1)
  glow:SetPoint("CENTER", 1, 0)
  glow:Hide()
  local glowAnim = glow:CreateAnimationGroup()
  glowAnim:SetLooping("REPEAT")
  local fadeIn = glowAnim:CreateAnimation("Alpha")
  fadeIn:SetFromAlpha(0); fadeIn:SetToAlpha(1); fadeIn:SetDuration(0.8); fadeIn:SetOrder(1); fadeIn:SetSmoothing("IN_OUT")
  local fadeOut = glowAnim:CreateAnimation("Alpha")
  fadeOut:SetFromAlpha(1); fadeOut:SetToAlpha(0); fadeOut:SetDuration(0.8); fadeOut:SetOrder(2); fadeOut:SetSmoothing("IN_OUT")
  glow:SetAlpha(0)

  -- Tooltip: what the button does, then one line per parked change -- the CVar on
  -- the left, its current value, an arrow, and the target value on the right.
  applyBtn:SetScript("OnEnter", function(self)
    GameTooltip:SetOwner(self, "ANCHOR_BOTTOMLEFT")
    GameTooltip_SetTitle(GameTooltip, "Apply pending changes")
    GameTooltip_AddNormalLine(GameTooltip, "Some changes are staged rather than applied the moment you make them - because they might briefly freeze the game or move the UI.", true)
    GameTooltip_AddInstructionLine(GameTooltip, "Click to apply the changes below now.", true)
    GameTooltip:AddLine(" ")
    for cvar, target in pairs(pending) do
      GameTooltip:AddDoubleLine(cvar, (GetCVar(cvar) or "?") .. CreateAtlasMarkup("arrow-short", 21, 18) .. target, 0.8, 0.8, 0.8)
    end
    GameTooltip:Show()
  end)
  applyBtn:SetScript("OnLeave", GameTooltip_Hide)

  local function ApplyPending()
    if not next(pending) then return end
    -- Snapshot the pre-apply values (and note whether the batch needs a confirm) before the
    -- SetCVars overwrite them; the freeze (if any) happens inside CommitValues, deliberately.
    local snapshot, needsConfirm = {}, false
    for cvar in pairs(pending) do
      snapshot[cvar] = GetCVar(cvar)
      if IsConfirmApply(cvar) then needsConfirm = true end
    end
    CommitValues(pending)
    wipe(pending)
    RefreshApplyButton()
    if needsConfirm then
      -- Persist the whole batch's old values, then guard the apply with a countdown that
      -- reverts to them unless kept (and on the next clean load if the client is force-closed).
      Graphit_config = Graphit_config or {}
      Graphit_config.confirmSnapshot = snapshot
      StaticPopup_Show("GRAPHIT_CONFIRM_APPLY")
    end
  end
  applyBtn:SetScript("OnClick", ApplyPending)

  -- Clicking Blizzard's graphics Apply applies our parked changes too, so its
  -- click commits everything and our button clears alongside it. (The reverse --
  -- forcing Blizzard's button to show for our pending -- fights its own dirty
  -- tracking and is left out.) Blizzard_Settings_Shared is not load-on-demand, so
  -- the button exists by now; we hook the button (not Commit, which also fires on
  -- panel close) so only an explicit Apply triggers it.
  if SettingsPanel and SettingsPanel.ApplyButton then
    SettingsPanel.ApplyButton:HookScript("OnClick", ApplyPending)
  end

  -- Assign the forward-declared updater the bindings call: show + pulse while
  -- anything is pending, hide otherwise, refresh an open tooltip in place, and
  -- re-tint the pending rows' labels.
  RefreshApplyButton = function()
    if next(pending) then
      applyBtn:Show()
      if not glowAnim:IsPlaying() then glow:Show(); glowAnim:Play() end
    else
      glowAnim:Stop(); glow:Hide()
      applyBtn:Hide()
    end
    if applyBtn:IsShown() and applyBtn:IsMouseOver() then
      applyBtn:GetScript("OnEnter")(applyBtn)
    end
    HighlightPending(content)
  end
  RefreshApplyButton()

  -- ===== Live sync with the Settings panel (inward) =====
  -- Outward sync (Graphit edit -> panel) is handled by SetCVar via
  -- RefreshSettingsPanel. Inward (panel/console/macro -> Graphit): watch the CVars
  -- we display and refresh on change -- coalesced, and delayed by CASCADE_SETTLE so
  -- a secondary changed elsewhere is read only after the engine has cascaded it to its
  -- tertiaries. Only while shown; a change made while hidden is picked up by the
  -- OnShow refresh below.
  f:HookScript("OnShow", function() RefreshAllRows(content) end)

  if CVarCallbackRegistry then
    local syncPending = false
    local function ScheduleSyncRefresh()
      if syncPending or not f:IsShown() then return end
      syncPending = true
      C_Timer.After(CASCADE_SETTLE, function()
        syncPending = false
        if f:IsShown() then RefreshAllRows(content) end
      end)
    end
    local watched = {}
    for _, s in ipairs(Graphit.layer2) do watched[s.cvar] = true end
    for _, tertiaries in pairs(Graphit.layer3) do
      for tertiaryCvar in pairs(tertiaries) do watched[tertiaryCvar] = true end
    end
    for _, s in ipairs(Graphit.layer1) do  -- the standalone settings (vsync, ...)
      if s.cvar then watched[s.cvar] = true end
      -- a compound checkbox+slider also drives an enable CVar (useMaxFPS, useUiScale, ...)
      if s.control and s.control.check then watched[s.control.check] = true end
    end
    for cvar in pairs(watched) do
      CVarCallbackRegistry:RegisterCallback(cvar, ScheduleSyncRefresh, f)
    end

    -- A Graphics API / Graphics Card change (gxapi / gxAdapter) only lands on a
    -- graphics-device restart, and its CVar callback is unreliable for that path (see
    -- the GX_RESTARTED note in Graphics.lua) -- so also refresh when the device restarts,
    -- e.g. after the Settings panel applies one.
    if EventRegistry then
      EventRegistry:RegisterFrameEvent("GX_RESTARTED")
      EventRegistry:RegisterCallback("GX_RESTARTED", function() ScheduleSyncRefresh() end, f)

      -- A UI Scale change (uiscale / useUiScale) signals via UI_SCALE_CHANGED, not a
      -- reliable CVar callback -- so the Settings panel applying one lands here.
      EventRegistry:RegisterFrameEvent("UI_SCALE_CHANGED")
      EventRegistry:RegisterCallback("UI_SCALE_CHANGED", function() ScheduleSyncRefresh() end, f)

      -- Resolution is CVar-less (C_VideoOptions), so its only change signal is the window
      -- resize -- DISPLAY_SIZE_CHANGED catches the Settings panel applying a new one.
      EventRegistry:RegisterFrameEvent("DISPLAY_SIZE_CHANGED")
      EventRegistry:RegisterCallback("DISPLAY_SIZE_CHANGED", function() ScheduleSyncRefresh() end, f)
    end
  end

  -- OnUpdate (runs only while shown): row hover every frame, plus FPS measured
  -- from real frame deltas over a short window so it tracks the true value within
  -- FPS_INTERVAL (GetFramerate() is engine-smoothed and lags ~2-3s).
  -- Hidden rows (collapsed tertiaries) report IsMouseOver() false, so the hover
  -- pass is safe to run over all registered rows.
  local fpsFrames, fpsElapsed = 0, 0
  f:SetScript("OnUpdate", function(self, dt)
    for _, row in ipairs(hoverRows) do
      row.highlight:SetShown(row:IsMouseOver())
    end
    fpsFrames  = fpsFrames + 1
    fpsElapsed = fpsElapsed + dt
    if fpsElapsed >= FPS_INTERVAL then
      UpdateFPS(self, fpsFrames / fpsElapsed)
      fpsFrames, fpsElapsed = 0, 0
    end
  end)
  UpdateFPS(f, GetFramerate())  -- initial value before the first measured window

  f:Hide()  -- start hidden; the first Toggle shows it
  -- Open / close sounds, matching Blizzard's options windows. Hooked after the initial Hide above,
  -- so building the frame does not play the close sound.
  f:HookScript("OnShow", function() PlaySound(SOUNDKIT.IG_MAINMENU_OPEN) end)
  f:HookScript("OnHide", function() PlaySound(SOUNDKIT.IG_MAINMENU_CLOSE) end)
  return f
end

